  PROCEDURE DETAIL_INSERT (
      P_REQ_ID NUMBER, P_EMP_NO NUMBER, P_REQ_AMOUNT NUMBER,
      P_NOTE VARCHAR2, P_MSG_OUT OUT VARCHAR2
  ) IS
    V_ST NUMBER; V_TYPE NUMBER; V_CALC NUMBER; V_PCT NUMBER; V_MONTH NUMBER;
    V_MASTER_PT NUMBER; V_LV NUMBER; V_HV NUMBER;
    V_USER NUMBER; V_ID NUMBER;
    V_MONTH2 NUMBER; V_AMT NUMBER; V_PT NUMBER; V_DA NUMBER; V_LF VARCHAR2(3); V_MSG VARCHAR2(4000);
    V_EX NUMBER := 0;
  BEGIN
    SELECT STATUS, REQ_TYPE, CALC_METHOD, PERCENT_VAL, THE_MONTH, PAY_TYPE, L_VALUE, H_VALUE
      INTO V_ST, V_TYPE, V_CALC, V_PCT, V_MONTH, V_MASTER_PT, V_LV, V_HV
      FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID;

    IF V_ST NOT IN (C_REQ_STATUS_DRAFT, C_REQ_STATUS_PARTIAL_APPROVED, C_REQ_STATUS_PARTIAL_PAID) THEN
      P_MSG_OUT := 'لا يمكن إضافة موظف بهذه الحالة'; RETURN;
    END IF;

    SELECT COUNT(1) INTO V_EX FROM GFC.PAYMENT_REQ_DETAIL_TB
     WHERE REQ_ID = P_REQ_ID AND EMP_NO = P_EMP_NO;
    IF V_EX > 0 THEN P_MSG_OUT := 'الموظف موجود مسبقا في هذا الطلب'; RETURN; END IF;

    -- فحص تكرار: الموظف في طلب آخر من نفس النوع لنفس الشهر
    V_EX := 0;
    SELECT COUNT(1) INTO V_EX FROM GFC.PAYMENT_REQ_DETAIL_TB D
     JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
     WHERE D.EMP_NO = P_EMP_NO AND D.DETAIL_STATUS <> C_REQ_STATUS_CANCELLED
       AND M.STATUS <> C_REQ_STATUS_CANCELLED
       AND M.THE_MONTH = V_MONTH AND M.REQ_TYPE = V_TYPE
       AND M.REQ_ID <> P_REQ_ID;
    IF V_EX > 0 THEN P_MSG_OUT := 'الموظف موجود في طلب آخر من نفس النوع لنفس الشهر'; RETURN; END IF;

    PREPARE_REQUEST_VALUES(P_EMP_NO, V_MONTH, V_TYPE,
        V_CALC, V_PCT, P_REQ_AMOUNT, V_MASTER_PT,
        NULL, P_REQ_ID, V_LV, V_HV,
        V_MONTH2, V_AMT, V_PT, V_DA, V_LF, V_MSG);
    IF V_MSG IS NOT NULL THEN P_MSG_OUT := V_MSG; RETURN; END IF;

    V_USER := USER_PKG.GET_USER_ID;
    V_ID   := PAYMENT_REQ_DETAIL_SEQ.NEXTVAL;

    INSERT INTO GFC.PAYMENT_REQ_DETAIL_TB (
        DETAIL_ID, REQ_ID, EMP_NO, REQ_AMOUNT, DUES_AMOUNT,
        PAY_TYPE, LIMIT_FLAG, DETAIL_STATUS, NOTE, ENTRY_DATE, ENTRY_USER
    ) VALUES (
        V_ID, P_REQ_ID, P_EMP_NO, V_AMT, V_DA,
        V_PT, V_LF, C_REQ_STATUS_DRAFT, P_NOTE, SYSDATE, V_USER
    );

    SYNC_MASTER_AMOUNT(P_REQ_ID);
    -- تحديث حالة الماستر (إضافة مسودة لطلب جزئي)
    IF V_ST <> C_REQ_STATUS_DRAFT THEN
      DECLARE V_NEW_ST NUMBER;
      BEGIN
        V_NEW_ST := CALC_MASTER_STATUS(P_REQ_ID);
        UPDATE GFC.PAYMENT_REQ_TB SET
            STATUS = V_NEW_ST,
            UPDATE_USER = USER_PKG.GET_USER_ID, UPDATE_DATE = SYSDATE
         WHERE REQ_ID = P_REQ_ID;
      END;
    END IF;
    LOG_ACTION(P_REQ_ID, 'ADD_EMP', NULL, NULL, 'EMP_NO=' || TO_CHAR(P_EMP_NO) || ' AMT=' || TO_CHAR(V_AMT));
    P_MSG_OUT := TO_CHAR(V_ID);
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


