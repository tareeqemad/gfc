  PROCEDURE PAYMENT_REQ_APPROVE_BATCH (P_THE_MONTH NUMBER, P_REQ_IDS VARCHAR2 DEFAULT NULL, P_MSG_OUT OUT VARCHAR2) IS
    V_OK NUMBER := 0; V_ERR NUMBER := 0; V_MSG VARCHAR2(4000);
  BEGIN
    FOR R IN (SELECT REQ_ID FROM GFC.PAYMENT_REQ_TB
              WHERE STATUS = C_REQ_STATUS_DRAFT AND THE_MONTH = P_THE_MONTH
              AND (P_REQ_IDS IS NULL OR INSTR(','||P_REQ_IDS||',', ','||TO_CHAR(REQ_ID)||',') > 0)
              ORDER BY REQ_ID)
    LOOP
      BEGIN
        V_MSG := NULL;
        PAYMENT_REQ_APPROVE(R.REQ_ID, V_MSG);
        IF V_MSG = '1' THEN V_OK := V_OK + 1;
        ELSE V_ERR := V_ERR + 1;
          BEGIN INSERT INTO GFC.PROCESS_LOG_TB (LOG_ID,EMP_NO,THE_MONTH,PROCESS_NAME,ERROR_MSG,LOG_DATE)
                VALUES (PROCESS_LOG_SEQ.NEXTVAL,0,P_THE_MONTH,'APPROVE_BATCH',SUBSTR(V_MSG,1,500),SYSDATE);
          EXCEPTION WHEN OTHERS THEN NULL; END;
        END IF;
      EXCEPTION WHEN OTHERS THEN V_ERR := V_ERR + 1; END;
    END LOOP;
    P_MSG_OUT := TO_CHAR(V_OK) || '|' || TO_CHAR(V_ERR);
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := '0|1|' || SUBSTR(SQLERRM,1,200);
  END;


  PROCEDURE PAYMENT_REQ_PAY_BATCH (P_THE_MONTH NUMBER, P_REQ_IDS VARCHAR2 DEFAULT NULL, P_MSG_OUT OUT VARCHAR2) IS
    V_OK NUMBER := 0; V_ERR NUMBER := 0; V_MSG VARCHAR2(4000);
  BEGIN
    FOR R IN (SELECT REQ_ID FROM GFC.PAYMENT_REQ_TB
