    V_T1  CONSTANT NUMBER := C_REQ_TYPE_FULL_SALARY;
    V_T2  CONSTANT NUMBER := C_REQ_TYPE_PART_SALARY;
    V_T3  CONSTANT NUMBER := C_REQ_TYPE_LUMP_SUM;
  BEGIN
    OPEN REF_CUR_OUT FOR
      'SELECT
        COUNT(1) AS CNT,
        NVL(SUM(D.REQ_AMOUNT), 0) AS TOTAL_AMOUNT,
        NVL(SUM(CASE WHEN M.STATUS <> '||V_CAN||' THEN D.REQ_AMOUNT END), 0) AS ACTIVE_AMOUNT,
        COUNT(CASE WHEN M.STATUS <> '||V_CAN||' THEN 1 END) AS ACTIVE_COUNT,
        NVL(SUM(CASE WHEN M.STATUS = '||V_CAN||' THEN D.REQ_AMOUNT END), 0) AS CANCELLED_AMOUNT,
        COUNT(CASE WHEN M.STATUS = '||V_CAN||' THEN 1 END) AS CANCELLED_COUNT,
        NVL(SUM(CASE WHEN M.REQ_TYPE='||V_T1||' AND M.STATUS<>'||V_CAN||' THEN D.REQ_AMOUNT END), 0) AS T1_AMOUNT,
        COUNT(CASE WHEN M.REQ_TYPE='||V_T1||' AND M.STATUS<>'||V_CAN||' THEN 1 END) AS T1_COUNT,
        NVL(SUM(CASE WHEN M.REQ_TYPE='||V_T2||' AND M.STATUS<>'||V_CAN||' THEN D.REQ_AMOUNT END), 0) AS T2_AMOUNT,
        COUNT(CASE WHEN M.REQ_TYPE='||V_T2||' AND M.STATUS<>'||V_CAN||' THEN 1 END) AS T2_COUNT,
        NVL(SUM(CASE WHEN M.REQ_TYPE='||V_T3||' AND M.STATUS<>'||V_CAN||' THEN D.REQ_AMOUNT END), 0) AS T3_AMOUNT,
        COUNT(CASE WHEN M.REQ_TYPE='||V_T3||' AND M.STATUS<>'||V_CAN||' THEN 1 END) AS T3_COUNT,
        NVL(SUM(CASE WHEN M.STATUS='||V_DRF||' THEN D.REQ_AMOUNT END), 0) AS S0_AMOUNT,
        COUNT(CASE WHEN M.STATUS='||V_DRF||' THEN 1 END) AS S0_COUNT,
        NVL(SUM(CASE WHEN M.STATUS='||V_APP||' THEN D.REQ_AMOUNT END), 0) AS S1_AMOUNT,
        COUNT(CASE WHEN M.STATUS='||V_APP||' THEN 1 END) AS S1_COUNT,
        NVL(SUM(CASE WHEN M.STATUS='||V_PAD||' THEN D.REQ_AMOUNT END), 0) AS S2_AMOUNT,
        COUNT(CASE WHEN M.STATUS='||V_PAD||' THEN 1 END) AS S2_COUNT
      FROM GFC.PAYMENT_REQ_TB M
      JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.REQ_ID = M.REQ_ID
      WHERE 1=1 ' || P_INSQL;
    MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_TOTALS_BRANCH (P_INSQL VARCHAR2, REF_CUR_OUT OUT SYS_REFCURSOR, MSG_OUT OUT VARCHAR2) IS
    V_CAN CONSTANT NUMBER := C_REQ_STATUS_CANCELLED;
    V_PAD CONSTANT NUMBER := C_REQ_STATUS_PAID;
  BEGIN
    OPEN REF_CUR_OUT FOR
      'SELECT
        B.BRANCH_NO,
        B.BRANCH_NAME,
        COUNT(DISTINCT B.REQ_ID) AS REQ_COUNT,
        NVL(SUM(B.REQ_AMOUNT), 0) AS REQ_AMOUNT,
        COUNT(DISTINCT CASE WHEN B.M_STATUS='||V_PAD||' THEN B.REQ_ID END) AS PAID_COUNT,
        NVL(SUM(CASE WHEN B.M_STATUS='||V_PAD||' THEN B.REQ_AMOUNT END), 0) AS PAID_AMOUNT
      FROM (
        SELECT M.REQ_ID, M.REQ_AMOUNT, M.STATUS AS M_STATUS,
               EMP_PKG.GET_EMP_BRANCH(D.EMP_NO) AS BRANCH_NO,
               SETTING_PKG.GCC_BRANCHES_TB_GET_NAME(EMP_PKG.GET_EMP_BRANCH(D.EMP_NO)) AS BRANCH_NAME
          FROM GFC.PAYMENT_REQ_TB M
          JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.REQ_ID = M.REQ_ID
         WHERE M.STATUS <> '||V_CAN||' ' || NVL(P_INSQL, '') || '
      ) B
      GROUP BY B.BRANCH_NO, B.BRANCH_NAME
      ORDER BY REQ_AMOUNT DESC';
    MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN MSG_OUT := SQLERRM;
  END;


