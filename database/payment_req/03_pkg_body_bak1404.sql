CREATE OR REPLACE PACKAGE BODY GFC_PAK.DISBURSEMENT_PKG AS

  -- ============================================================
  -- Private Helpers
  -- ============================================================

  FUNCTION GET_DEFAULT_MONTH RETURN NUMBER IS
  BEGIN RETURN TO_NUMBER(TO_CHAR(SYSDATE, 'YYYYMM')); END;

  FUNCTION BUILD_REQUEST_NO (P_REQ_ID NUMBER) RETURN VARCHAR2 IS
  BEGIN RETURN 'PR-' || LPAD(P_REQ_ID, 5, '0');
  EXCEPTION WHEN OTHERS THEN RETURN TO_CHAR(P_REQ_ID); END;

  FUNCTION GET_EMP_BANK_NAME (P_EMP_NO NUMBER) RETURN VARCHAR2 IS
    V_NAME VARCHAR2(50);
  BEGIN
    SELECT B.NAME INTO V_NAME
      FROM DATA.EMPLOYEES E
      JOIN DATA.BANKS B ON B.NO = E.BANK
     WHERE E.NO = P_EMP_NO;
    RETURN V_NAME;
  EXCEPTION WHEN OTHERS THEN RETURN NULL;
  END;

  FUNCTION GET_EMP_IS_ACTIVE (P_EMP_NO NUMBER) RETURN NUMBER IS
    V NUMBER;
  BEGIN
    SELECT IS_ACTIVE INTO V FROM DATA.EMPLOYEES WHERE NO = P_EMP_NO;
    RETURN NVL(V, 0);
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN 0; END;

  FUNCTION EMP_HAS_MONTHLY_SALARY (P_EMP_NO NUMBER, P_THE_MONTH NUMBER) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT COUNT(1) INTO V FROM DATA.EMPLOYEES_MONTH WHERE NO = P_EMP_NO AND THE_MONTH = P_THE_MONTH;
    RETURN CASE WHEN V > 0 THEN 1 ELSE 0 END;
  EXCEPTION WHEN OTHERS THEN RETURN 0; END;

  FUNCTION GET_BANK_NAME (P_BANK_NO NUMBER) RETURN VARCHAR2 IS
    V VARCHAR2(200);
  BEGIN
    SELECT NAME INTO V FROM DATA.BANKS WHERE NO = P_BANK_NO AND ROWNUM = 1;
    RETURN V;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  -- اسم البنك الرئيسي: EMPLOYEES.MASTER_BANKS_EMAIL → MASTER_BANKS_EMAIL.B_NAME
  -- P_EMP_NO: رقم الموظف (مش رقم البنك)
  FUNCTION GET_MASTER_BANK_NAME (P_EMP_NO NUMBER) RETURN VARCHAR2 IS
    V_MB_NO NUMBER; V_NAME VARCHAR2(200);
  BEGIN
    SELECT MASTER_BANKS_EMAIL INTO V_MB_NO FROM DATA.EMPLOYEES WHERE NO = P_EMP_NO AND ROWNUM = 1;
    IF V_MB_NO IS NULL OR V_MB_NO = 0 THEN RETURN NULL; END IF;
    SELECT B_NAME INTO V_NAME FROM DATA.MASTER_BANKS_EMAIL WHERE B_NO = V_MB_NO AND ROWNUM = 1;
    RETURN V_NAME;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; WHEN OTHERS THEN RETURN NULL;
  END;

  -- بنود الاستحقاقات (DATA.CONSTANT مع IS_ADD=1 AND IS_CONSTANT=2)
  PROCEDURE GET_BENEFIT_ITEMS (
      P_REF_CUR_OUT OUT SYS_REFCURSOR, P_MSG_OUT OUT VARCHAR2
  ) IS
  BEGIN
    OPEN P_REF_CUR_OUT FOR
      SELECT C.NO, C.NAME FROM DATA.CONSTANT C
       WHERE C.IS_ADD = 1 AND C.IS_CONSTANT = 2
       ORDER BY C.NO;
    P_MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;

  FUNCTION GET_CONSTANT_NAME (P_CON_NO NUMBER) RETURN VARCHAR2 IS
    V VARCHAR2(200);
  BEGIN
    SELECT NAME INTO V FROM DATA.CONSTANT WHERE NO = P_CON_NO AND ROWNUM = 1;
    RETURN V;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  FUNCTION GET_DUES_TYPE_NAME (P_PAY_TYPE NUMBER) RETURN VARCHAR2 IS
    V VARCHAR2(200);
  BEGIN
    SELECT TYPE_NAME INTO V FROM GFC.SALARY_DUES_TYPES_TB WHERE TYPE_ID = P_PAY_TYPE AND ROWNUM = 1;
    RETURN V;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  FUNCTION GET_DUES_TYPE_LINE (P_PAY_TYPE NUMBER) RETURN NUMBER IS
    V NUMBER;
  BEGIN
    SELECT LINE_TYPE INTO V FROM GFC.SALARY_DUES_TYPES_TB WHERE TYPE_ID = P_PAY_TYPE AND NVL(IS_ACTIVE,1) = 1;
    RETURN V;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  FUNCTION IS_DUES_TYPE_LEAF (P_PAY_TYPE NUMBER) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT COUNT(1) INTO V FROM GFC.SALARY_DUES_TYPES_TB WHERE PARENT_ID = P_PAY_TYPE AND NVL(IS_ACTIVE,1) = 1;
    RETURN CASE WHEN V > 0 THEN 0 ELSE 1 END;
  EXCEPTION WHEN OTHERS THEN RETURN 0; END;

  PROCEDURE SYNC_MASTER_AMOUNT (P_REQ_ID NUMBER) IS
  BEGIN
    UPDATE GFC.PAYMENT_REQ_TB SET REQ_AMOUNT = (
      SELECT NVL(SUM(REQ_AMOUNT), 0) FROM GFC.PAYMENT_REQ_DETAIL_TB
       WHERE REQ_ID = P_REQ_ID AND DETAIL_STATUS <> C_REQ_STATUS_CANCELLED
    ) WHERE REQ_ID = P_REQ_ID;
  END;

  -- حساب حالة الماستر من حالات الديتيل
  FUNCTION CALC_MASTER_STATUS (P_REQ_ID NUMBER) RETURN NUMBER IS
    V_TOTAL NUMBER; V_DRAFT NUMBER; V_APPROVED NUMBER; V_PAID NUMBER; V_CANCELLED NUMBER; V_BATCH NUMBER;
    V_ACTIVE NUMBER;
  BEGIN
    SELECT COUNT(1),
           COUNT(CASE WHEN DETAIL_STATUS = C_REQ_STATUS_DRAFT THEN 1 END),
           COUNT(CASE WHEN DETAIL_STATUS = C_REQ_STATUS_APPROVED THEN 1 END),
           COUNT(CASE WHEN DETAIL_STATUS = C_REQ_STATUS_PAID THEN 1 END),
           COUNT(CASE WHEN DETAIL_STATUS = C_REQ_STATUS_CANCELLED THEN 1 END),
           COUNT(CASE WHEN DETAIL_STATUS = C_REQ_STATUS_PARTIAL_PAID THEN 1 END)
      INTO V_TOTAL, V_DRAFT, V_APPROVED, V_PAID, V_CANCELLED, V_BATCH
      FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID;

    V_ACTIVE := V_TOTAL - V_CANCELLED;

    IF V_ACTIVE = 0 THEN RETURN C_REQ_STATUS_DRAFT; END IF;

    -- كل الفعّالين مصروفين
    IF V_PAID = V_ACTIVE THEN RETURN C_REQ_STATUS_PAID; END IF;

    -- في مصروفين فعلياً (PAID=2) + غيرهم = مصروف جزئي
    IF V_PAID > 0 THEN RETURN C_REQ_STATUS_PAID; END IF;

    -- كل أو بعض الفعّالين محتسبين (بدفعة) = محتسب
    IF V_BATCH > 0 THEN RETURN C_REQ_STATUS_PARTIAL_PAID; END IF;

    -- كلهم معتمدين
    IF V_APPROVED = V_ACTIVE THEN RETURN C_REQ_STATUS_APPROVED; END IF;

    -- يوجد مسودة + معتمدين = معتمد جزئي
    IF V_APPROVED > 0 THEN RETURN C_REQ_STATUS_PARTIAL_APPROVED; END IF;

    -- كلهم مسودة
    RETURN C_REQ_STATUS_DRAFT;
  END;

  -- ============================================================
  -- LOG — تسجيل كل عملية بجدول PAYMENT_REQ_LOG_TB
  -- ============================================================
  PROCEDURE LOG_ACTION (
      P_REQ_ID NUMBER, P_ACTION VARCHAR2,
      P_OLD_STATUS NUMBER, P_NEW_STATUS NUMBER,
      P_NOTE VARCHAR2 DEFAULT NULL
  ) IS
  BEGIN
    INSERT INTO GFC.PAYMENT_REQ_LOG_TB (
        LOG_ID, REQ_ID, ACTION, OLD_STATUS, NEW_STATUS,
        ACTION_USER, ACTION_DATE, NOTE, EMP_COUNT, TOTAL_AMOUNT
    ) VALUES (
        PAYMENT_REQ_LOG_SEQ.NEXTVAL, P_REQ_ID, P_ACTION,
        P_OLD_STATUS, P_NEW_STATUS,
        USER_PKG.GET_USER_ID, SYSDATE, SUBSTR(P_NOTE, 1, 500),
        (SELECT COUNT(1) FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID),
        (SELECT NVL(SUM(REQ_AMOUNT),0) FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID)
    );
  EXCEPTION WHEN OTHERS THEN NULL;
  END;

  -- ============================================================
  -- PREPARE_REQUEST_VALUES — حساب المبالغ حسب نوع الطلب
  -- ============================================================
  PROCEDURE PREPARE_REQUEST_VALUES (
      P_EMP_NO NUMBER, P_THE_MONTH NUMBER, P_REQ_TYPE NUMBER,
      P_CALC_METHOD NUMBER, P_PERCENT_VAL NUMBER,
      P_REQ_AMOUNT NUMBER, P_PAY_TYPE NUMBER,
      P_DUES_AMOUNT NUMBER, P_EXCLUDE_REQ_ID NUMBER,
      P_L_VALUE NUMBER DEFAULT NULL, P_H_VALUE NUMBER DEFAULT NULL,
      O_THE_MONTH OUT NUMBER, O_REQ_AMOUNT OUT NUMBER, O_PAY_TYPE OUT NUMBER,
      O_DUES_AMOUNT OUT NUMBER, O_LIMIT_FLAG OUT VARCHAR2, O_MSG OUT VARCHAR2
  ) IS
    V_MONTH NUMBER; V_DUE_AVAIL NUMBER;
    V_AMOUNT NUMBER; V_323 NUMBER; V_CALC NUMBER;
  BEGIN
    O_MSG := NULL; O_LIMIT_FLAG := NULL;
    V_MONTH := NVL(P_THE_MONTH, GET_DEFAULT_MONTH);
    O_THE_MONTH := V_MONTH; O_REQ_AMOUNT := 0; O_PAY_TYPE := NULL;
    O_DUES_AMOUNT := 0;

    IF P_EMP_NO IS NULL THEN O_MSG := 'رقم الموظف مطلوب'; RETURN; END IF;
    IF P_REQ_TYPE NOT IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM, C_REQ_TYPE_MONTHLY_DUES, C_REQ_TYPE_BENEFITS) THEN O_MSG := 'نوع الطلب غير صحيح'; RETURN; END IF;

    -- منع التكرار: نوع 1 و 2 و 4 بيتنافسو على نفس البند (323) — لا يسمح بأكثر من طلب نشط لنفس الموظف + الشهر
    IF P_REQ_TYPE IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_MONTHLY_DUES) THEN
      DECLARE V_EX_CNT NUMBER;
      BEGIN
        SELECT COUNT(1) INTO V_EX_CNT
          FROM GFC.PAYMENT_REQ_DETAIL_TB D
          JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
         WHERE D.EMP_NO = P_EMP_NO
           AND M.THE_MONTH = V_MONTH
           AND M.REQ_TYPE IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_MONTHLY_DUES)
           AND M.STATUS <> C_REQ_STATUS_CANCELLED
           AND D.DETAIL_STATUS <> C_REQ_STATUS_CANCELLED
           AND (P_EXCLUDE_REQ_ID IS NULL OR M.REQ_ID <> P_EXCLUDE_REQ_ID);
        IF V_EX_CNT > 0 THEN
          O_MSG := 'الموظف موجود مسبقاً في طلب صرف راتب نشط لنفس الشهر';
          RETURN;
        END IF;
      END;
    END IF;

    IF P_REQ_TYPE = C_REQ_TYPE_FULL_SALARY THEN
      IF EMP_HAS_MONTHLY_SALARY(P_EMP_NO, V_MONTH) <> 1 THEN O_MSG := 'لم يتم احتساب الراتب'; RETURN; END IF;
      BEGIN
        SELECT NVL(SUM(S.VALUE), 0) INTO V_AMOUNT
          FROM DATA.SALARY S WHERE S.EMP_NO = P_EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_MONTH;
      EXCEPTION WHEN OTHERS THEN V_AMOUNT := 0; END;
      IF V_AMOUNT <= 0 THEN O_MSG := 'لا يوجد مستحقات (بند 323) لهذا الشهر'; RETURN; END IF;
      V_DUE_AVAIL := GET_EMP_DUES_AVAILABLE(P_EMP_NO, P_EXCLUDE_REQ_ID);
      IF V_AMOUNT > V_DUE_AVAIL THEN O_MSG := 'المبلغ يتجاوز رصيد المستحقات. المتاح=' || TO_CHAR(V_DUE_AVAIL); RETURN; END IF;
      O_REQ_AMOUNT := V_AMOUNT; O_DUES_AMOUNT := V_AMOUNT;
      O_PAY_TYPE := NVL(P_PAY_TYPE, C_DUES_PAYTYPE_SALARY_CONV);
      RETURN;
    END IF;

    IF P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN
      IF EMP_HAS_MONTHLY_SALARY(P_EMP_NO, V_MONTH) <> 1 THEN O_MSG := 'لم يتم احتساب الراتب'; RETURN; END IF;
      IF P_CALC_METHOD NOT IN (C_CALC_PERCENT, C_CALC_FIXED) THEN O_MSG := 'طريقة الاحتساب غير صحيحة'; RETURN; END IF;

      -- قيمة بند 323
      BEGIN
        SELECT NVL(SUM(S.VALUE), 0) INTO V_323
          FROM DATA.SALARY S WHERE S.EMP_NO = P_EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_MONTH;
      EXCEPTION WHEN OTHERS THEN V_323 := 0; END;
      IF V_323 <= 0 THEN O_MSG := 'لا يوجد مستحقات (بند 323) لهذا الشهر'; RETURN; END IF;

      IF P_CALC_METHOD = C_CALC_PERCENT THEN
        IF NVL(P_PERCENT_VAL, 0) < 0 OR P_PERCENT_VAL > 100 THEN O_MSG := 'النسبة 0-100'; RETURN; END IF;
        V_CALC := ROUND(V_323 * P_PERCENT_VAL / 100, 2);
        V_AMOUNT := V_CALC;

        -- تطبيق الحدود (لو محددة) مع تسجيل LIMIT_FLAG
        IF P_L_VALUE IS NOT NULL AND V_CALC < P_L_VALUE THEN
          IF V_323 < P_L_VALUE THEN
            V_AMOUNT := V_323; -- ياخد كله (بند 323 أقل من الحد الأدنى)
            O_LIMIT_FLAG := 'CAP';
          ELSE
            V_AMOUNT := P_L_VALUE; -- يرتفع للحد الأدنى
            O_LIMIT_FLAG := 'MIN';
          END IF;
        END IF;
        IF P_H_VALUE IS NOT NULL AND V_CALC > P_H_VALUE THEN
          V_AMOUNT := P_H_VALUE; -- ينزل للحد الأعلى
          O_LIMIT_FLAG := 'MAX';
        END IF;
      ELSE
        IF NVL(P_REQ_AMOUNT, 0) <= 0 THEN O_MSG := 'المبلغ يجب أن يكون أكبر من صفر'; RETURN; END IF;
        V_AMOUNT := P_REQ_AMOUNT;
      END IF;

      -- نوع 2: المبلغ لا يتجاوز بند 323 (مش المحفظة)
      V_AMOUNT := LEAST(V_AMOUNT, V_323);
      IF V_AMOUNT <= 0 THEN O_MSG := 'لا يوجد مبلغ للصرف'; RETURN; END IF;
      O_REQ_AMOUNT := V_AMOUNT; O_DUES_AMOUNT := V_AMOUNT;
      O_PAY_TYPE := NVL(P_PAY_TYPE, C_DUES_PAYTYPE_SALARY_CONV);
      RETURN;
    END IF;

    IF P_REQ_TYPE = C_REQ_TYPE_LUMP_SUM THEN
      -- نوع 3: دفعة من المستحقات العام — نسبة أو مبلغ ثابت
      V_DUE_AVAIL := GET_EMP_DUES_AVAILABLE(P_EMP_NO, P_EXCLUDE_REQ_ID);
      IF V_DUE_AVAIL <= 0 THEN O_MSG := 'لا يوجد رصيد مستحقات متاح'; RETURN; END IF;

      IF P_CALC_METHOD = C_CALC_PERCENT THEN
        -- نسبة من رصيد المستحقات
        IF NVL(P_PERCENT_VAL, 0) <= 0 OR P_PERCENT_VAL > 100 THEN O_MSG := 'النسبة 1-100'; RETURN; END IF;
        V_AMOUNT := ROUND(V_DUE_AVAIL * P_PERCENT_VAL / 100, 2);
      ELSE
        -- مبلغ ثابت
        IF NVL(P_REQ_AMOUNT, 0) <= 0 THEN O_MSG := 'المبلغ يجب أن يكون أكبر من صفر'; RETURN; END IF;
        V_AMOUNT := P_REQ_AMOUNT;
      END IF;

      IF V_AMOUNT > V_DUE_AVAIL THEN V_AMOUNT := V_DUE_AVAIL; END IF;
      O_REQ_AMOUNT := V_AMOUNT; O_DUES_AMOUNT := V_AMOUNT;
      O_PAY_TYPE := NVL(P_PAY_TYPE, C_DUES_PAYTYPE_PAYMENT);
      RETURN;
    END IF;

    IF P_REQ_TYPE = C_REQ_TYPE_MONTHLY_DUES THEN
      -- نوع 4: المتبقي من بند 323 للشهر المحدد
      IF EMP_HAS_MONTHLY_SALARY(P_EMP_NO, V_MONTH) <> 1 THEN O_MSG := 'لم يتم احتساب الراتب'; RETURN; END IF;
      BEGIN
        SELECT NVL(SUM(S.VALUE), 0) INTO V_323
          FROM DATA.SALARY S WHERE S.EMP_NO = P_EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_MONTH;
      EXCEPTION WHEN OTHERS THEN V_323 := 0; END;
      IF V_323 <= 0 THEN O_MSG := 'لا يوجد مستحقات (بند 323) لهذا الشهر'; RETURN; END IF;

      -- حساب المصروف من نوع 1 و 2 لنفس الشهر
      BEGIN
        SELECT NVL(SUM(D.REQ_AMOUNT), 0) INTO V_CALC
          FROM GFC.PAYMENT_REQ_DETAIL_TB D
          JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
         WHERE D.EMP_NO = P_EMP_NO AND M.THE_MONTH = V_MONTH
           AND M.REQ_TYPE IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_MONTHLY_DUES)
           AND M.STATUS <> C_REQ_STATUS_CANCELLED
           AND D.DETAIL_STATUS <> C_REQ_STATUS_CANCELLED
           AND (P_EXCLUDE_REQ_ID IS NULL OR M.REQ_ID <> P_EXCLUDE_REQ_ID);
      EXCEPTION WHEN OTHERS THEN V_CALC := 0;
      END;
      V_CALC := V_323 - V_CALC;  -- المتبقي = بند 323 - المصروف
      IF V_CALC <= 0 THEN O_MSG := 'لا يوجد متبقي — تم صرف كامل بند 323 (' || TO_CHAR(V_323) || ')'; RETURN; END IF;

      IF NVL(P_REQ_AMOUNT, 0) > 0 THEN
        V_AMOUNT := LEAST(P_REQ_AMOUNT, V_CALC);  -- مبلغ يدوي مع سقف المتبقي
      ELSE
        V_AMOUNT := V_CALC;  -- كل المتبقي
      END IF;

      V_DUE_AVAIL := GET_EMP_DUES_AVAILABLE(P_EMP_NO, P_EXCLUDE_REQ_ID);
      IF V_AMOUNT > V_DUE_AVAIL THEN O_MSG := 'يتجاوز رصيد المستحقات. المتاح=' || TO_CHAR(V_DUE_AVAIL); RETURN; END IF;

      O_REQ_AMOUNT := V_AMOUNT; O_DUES_AMOUNT := V_AMOUNT;
      O_PAY_TYPE := NVL(P_PAY_TYPE, C_DUES_PAYTYPE_SALARY_CONV);
      RETURN;
    END IF;

    IF P_REQ_TYPE = C_REQ_TYPE_BENEFITS THEN
      -- نوع 5: صرف استحقاقات وإضافات (بدون تأثير على SALARY_DUES)
      IF NVL(P_REQ_AMOUNT, 0) <= 0 THEN O_MSG := 'المبلغ يجب أن يكون أكبر من صفر'; RETURN; END IF;
      O_REQ_AMOUNT := P_REQ_AMOUNT;
      O_DUES_AMOUNT := 0;  -- بدون تأثير على المستحقات
      O_PAY_TYPE := P_PAY_TYPE;
      RETURN;
    END IF;

    O_MSG := 'بيانات الطلب غير صحيحة';
  EXCEPTION WHEN OTHERS THEN O_MSG := SQLERRM;
  END PREPARE_REQUEST_VALUES;


  -- ============================================================
  -- Balance Functions
  -- ============================================================

  FUNCTION GET_EMP_NET_SALARY (P_EMP_NO NUMBER, P_THE_MONTH NUMBER) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT NVL(A.NET_SALARY, 0) INTO V FROM DATA.ADMIN A WHERE A.EMP_NO = P_EMP_NO AND A.MONTH = P_THE_MONTH;
    RETURN V;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN 0; END;

  FUNCTION GET_EMP_DUES_BASE (P_EMP_NO NUMBER) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT NVL(SUM(VALUE), 0) INTO V FROM DATA.SALARY
     WHERE EMP_NO = P_EMP_NO AND CON_NO IN (SELECT NO FROM DATA.CONSTANT WHERE CON_G = C_CONSTANT_GROUP_DUES);
    RETURN V;
  EXCEPTION WHEN OTHERS THEN RETURN 0; END;

  FUNCTION GET_EMP_DUES_ADD (P_EMP_NO NUMBER) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT NVL(SUM(M.PAY), 0) INTO V
      FROM GFC.SALARY_DUES_TB M JOIN GFC.SALARY_DUES_TYPES_TB T ON T.TYPE_ID = M.PAY_TYPE
     WHERE M.EMP_NO = P_EMP_NO AND NVL(M.STATUS,1) = 1 AND NVL(T.IS_ACTIVE,1) = 1 AND T.LINE_TYPE = C_LINE_ADD;
    RETURN V;
  EXCEPTION WHEN OTHERS THEN RETURN 0; END;

  FUNCTION GET_EMP_DUES_DED (P_EMP_NO NUMBER) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT NVL(SUM(M.PAY), 0) INTO V
      FROM GFC.SALARY_DUES_TB M JOIN GFC.SALARY_DUES_TYPES_TB T ON T.TYPE_ID = M.PAY_TYPE
     WHERE M.EMP_NO = P_EMP_NO AND NVL(M.STATUS,1) = 1 AND NVL(T.IS_ACTIVE,1) = 1 AND T.LINE_TYPE = C_LINE_DED;
    RETURN V;
  EXCEPTION WHEN OTHERS THEN RETURN 0; END;

  FUNCTION GET_EMP_DUES_RESERVED (P_EMP_NO NUMBER, P_EXCLUDE_REQ_ID NUMBER DEFAULT NULL) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT NVL(SUM(D.DUES_AMOUNT), 0) INTO V
      FROM GFC.PAYMENT_REQ_DETAIL_TB D
      JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
     WHERE D.EMP_NO = P_EMP_NO
       AND D.DETAIL_STATUS IN (C_REQ_STATUS_APPROVED, C_REQ_STATUS_PARTIAL_PAID)  -- محجوز = معتمد + محتسب (المسودة مش محجوز، المصروف راح على جدول المستحقات)
       AND M.STATUS <> C_REQ_STATUS_CANCELLED
       AND (P_EXCLUDE_REQ_ID IS NULL OR M.REQ_ID <> P_EXCLUDE_REQ_ID);
    RETURN V;
  END;

  FUNCTION GET_EMP_DUES_PAID (P_EMP_NO NUMBER, P_EXCLUDE_REQ_ID NUMBER DEFAULT NULL) RETURN NUMBER IS
    V NUMBER := 0;
  BEGIN
    SELECT NVL(SUM(D.DUES_AMOUNT), 0) INTO V
      FROM GFC.PAYMENT_REQ_DETAIL_TB D
      JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
     WHERE D.EMP_NO = P_EMP_NO
       AND D.DETAIL_STATUS = C_REQ_STATUS_PAID
       AND M.STATUS <> C_REQ_STATUS_CANCELLED
       AND (P_EXCLUDE_REQ_ID IS NULL OR M.REQ_ID <> P_EXCLUDE_REQ_ID);
    RETURN V;
  END;

  FUNCTION GET_EMP_DUES_AVAILABLE (P_EMP_NO NUMBER, P_EXCLUDE_REQ_ID NUMBER DEFAULT NULL) RETURN NUMBER IS
  BEGIN
    -- المتاح = أساسي + إضافات - خصومات (من SALARY_DUES_TB) - محجوز (معتمد فقط)
    -- المسودة: مش محجوز
    -- المعتمد: محجوز (لسا ما ترحّل)
    -- المصروف: مش محجوز (ترحّل لـ SALARY_DUES_TB كخصم — بينطرح عبر GET_EMP_DUES_DED)
    RETURN GREATEST(GET_EMP_DUES_BASE(P_EMP_NO)
       + GET_EMP_DUES_ADD(P_EMP_NO)
       - GET_EMP_DUES_DED(P_EMP_NO)
       - GET_EMP_DUES_RESERVED(P_EMP_NO, P_EXCLUDE_REQ_ID), 0);
  END;


  -- ============================================================
  -- Master CRUD
  -- ============================================================

  PROCEDURE PAYMENT_REQ_INSERT (
      P_THE_MONTH NUMBER, P_REQ_TYPE NUMBER,
      P_CALC_METHOD NUMBER, P_PERCENT_VAL NUMBER,
      P_L_VALUE NUMBER DEFAULT NULL, P_H_VALUE NUMBER DEFAULT NULL,
      P_PAY_TYPE NUMBER DEFAULT NULL, P_ENTRY_DATE DATE DEFAULT NULL,
      P_NOTE VARCHAR2, P_MSG_OUT OUT VARCHAR2
  ) IS
    V_ID NUMBER; V_NO VARCHAR2(50); V_USER NUMBER; V_DUP NUMBER;
    V_MONTH NUMBER := NVL(P_THE_MONTH, GET_DEFAULT_MONTH);
  BEGIN
    IF P_REQ_TYPE NOT IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM, C_REQ_TYPE_MONTHLY_DUES, C_REQ_TYPE_BENEFITS) THEN P_MSG_OUT := 'نوع الطلب غير صحيح'; RETURN; END IF;

    -- فحص تكرار: طلب من نفس النوع لنفس الشهر (أنواع 2,3,5 مستثناة — يسمح بأكثر من طلب)
    IF P_REQ_TYPE NOT IN (C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM, C_REQ_TYPE_BENEFITS) THEN
      SELECT COUNT(1) INTO V_DUP FROM GFC.PAYMENT_REQ_TB
       WHERE THE_MONTH = V_MONTH AND REQ_TYPE = P_REQ_TYPE AND STATUS <> C_REQ_STATUS_CANCELLED;
      IF V_DUP > 0 THEN
        P_MSG_OUT := 'يوجد طلب مفتوح من نفس النوع لنفس الشهر (' || TO_CHAR(V_MONTH) || ')';
        RETURN;
      END IF;
    END IF;

    V_ID   := PAYMENT_REQ_SEQ.NEXTVAL;
    V_NO   := BUILD_REQUEST_NO(V_ID);
    V_USER := USER_PKG.GET_USER_ID;

    INSERT INTO GFC.PAYMENT_REQ_TB (
        REQ_ID, REQ_NO, THE_MONTH, REQ_TYPE,
        CALC_METHOD, PERCENT_VAL, L_VALUE, H_VALUE, PAY_TYPE, REQ_AMOUNT,
        STATUS, NOTE, ENTRY_USER, ENTRY_DATE
    ) VALUES (
        V_ID, V_NO, V_MONTH, P_REQ_TYPE,
        CASE WHEN P_REQ_TYPE IN (C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM) THEN P_CALC_METHOD END,
        CASE WHEN P_REQ_TYPE IN (C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM) AND P_CALC_METHOD = C_CALC_PERCENT THEN P_PERCENT_VAL END,
        CASE WHEN P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN P_L_VALUE END,
        CASE WHEN P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN P_H_VALUE END,
        P_PAY_TYPE, 0, C_REQ_STATUS_DRAFT, P_NOTE, V_USER, NVL(P_ENTRY_DATE, SYSDATE)
    );

    LOG_ACTION(V_ID, 'CREATE', NULL, C_REQ_STATUS_DRAFT, P_NOTE);
    P_MSG_OUT := TO_CHAR(V_ID);
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_UPDATE (
      P_REQ_ID NUMBER, P_THE_MONTH NUMBER, P_REQ_TYPE NUMBER,
      P_CALC_METHOD NUMBER, P_PERCENT_VAL NUMBER,
      P_L_VALUE NUMBER DEFAULT NULL, P_H_VALUE NUMBER DEFAULT NULL,
      P_PAY_TYPE NUMBER DEFAULT NULL,
      P_NOTE VARCHAR2, P_MSG_OUT OUT VARCHAR2
  ) IS
    V_ST NUMBER; V_USER NUMBER;
    V_OLD_TYPE NUMBER; V_OLD_MONTH NUMBER;
    V_MONTH NUMBER; V_AMT NUMBER; V_PT NUMBER; V_DA NUMBER;
    V_LF VARCHAR2(3); V_MSG VARCHAR2(4000);
    V_OK NUMBER := 0; V_ERR NUMBER := 0;
  BEGIN
    SELECT STATUS, REQ_TYPE, THE_MONTH INTO V_ST, V_OLD_TYPE, V_OLD_MONTH
      FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID FOR UPDATE NOWAIT;
    IF V_ST <> C_REQ_STATUS_DRAFT THEN P_MSG_OUT := 'التعديل للمسودة فقط'; RETURN; END IF;

    V_USER := USER_PKG.GET_USER_ID;

    -- تحديث الماستر
    UPDATE GFC.PAYMENT_REQ_TB SET
        THE_MONTH = NVL(P_THE_MONTH, THE_MONTH),
        REQ_TYPE = P_REQ_TYPE,
        CALC_METHOD = CASE WHEN P_REQ_TYPE IN (C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM) THEN P_CALC_METHOD END,
        PERCENT_VAL = CASE WHEN P_REQ_TYPE IN (C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM) AND P_CALC_METHOD = C_CALC_PERCENT THEN P_PERCENT_VAL END,
        L_VALUE = CASE WHEN P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN P_L_VALUE END,
        H_VALUE = CASE WHEN P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN P_H_VALUE END,
        PAY_TYPE = CASE WHEN P_PAY_TYPE IS NOT NULL THEN P_PAY_TYPE ELSE PAY_TYPE END,
        NOTE = P_NOTE, UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE
     WHERE REQ_ID = P_REQ_ID;

    -- هل لازم نعيد حساب الموظفين؟
    IF V_OLD_TYPE <> P_REQ_TYPE THEN
      -- تغيير النوع → حذف كل الموظفين (البنية مختلفة)
      DELETE FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID;
      SYNC_MASTER_AMOUNT(P_REQ_ID);
      LOG_ACTION(P_REQ_ID, 'UPDATE_TYPE_CHANGE', V_ST, V_ST, 'نوع قديم=' || V_OLD_TYPE || ' جديد=' || P_REQ_TYPE || ' — تم حذف الموظفين');
      P_MSG_OUT := '2|تم تعديل النوع — الموظفين محذوفين ويجب إعادة إضافتهم';
      RETURN;
    END IF;

    -- نفس النوع — إعادة حساب المبالغ لكل موظف
    FOR D IN (SELECT DETAIL_ID, EMP_NO, REQ_AMOUNT, PAY_TYPE, DUES_AMOUNT
                FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID AND DETAIL_STATUS = C_REQ_STATUS_DRAFT)
    LOOP
      V_MSG := NULL;
      PREPARE_REQUEST_VALUES(D.EMP_NO, P_THE_MONTH, P_REQ_TYPE,
          P_CALC_METHOD, P_PERCENT_VAL, D.REQ_AMOUNT, D.PAY_TYPE,
          D.DUES_AMOUNT, P_REQ_ID, P_L_VALUE, P_H_VALUE,
          V_MONTH, V_AMT, V_PT, V_DA, V_LF, V_MSG);
      IF V_MSG IS NULL THEN
        UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET
            REQ_AMOUNT = V_AMT, PAY_TYPE = V_PT, DUES_AMOUNT = V_DA, LIMIT_FLAG = V_LF
         WHERE DETAIL_ID = D.DETAIL_ID;
        V_OK := V_OK + 1;
      ELSE
        V_ERR := V_ERR + 1;
      END IF;
    END LOOP;

    SYNC_MASTER_AMOUNT(P_REQ_ID);
    LOG_ACTION(P_REQ_ID, 'UPDATE', V_ST, V_ST, 'OK=' || V_OK || ' ERR=' || V_ERR);

    IF V_ERR > 0 THEN
      P_MSG_OUT := '1|تم التعديل — ' || V_OK || ' موظف محدّث، ' || V_ERR || ' فشل';
    ELSE
      P_MSG_OUT := '1';
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_APPROVE (P_REQ_ID NUMBER, P_MSG_OUT OUT VARCHAR2) IS
    R GFC.PAYMENT_REQ_TB%ROWTYPE;
    V_USER NUMBER; V_OK NUMBER := 0; V_ERR NUMBER := 0;
    V_FAIL_LIST VARCHAR2(4000) := '';
    V_MONTH NUMBER; V_AMT NUMBER; V_PT NUMBER; V_DA NUMBER;
    V_LF VARCHAR2(3); V_MSG VARCHAR2(4000);
    V_OLD_ST NUMBER; V_NEW_ST NUMBER;
  BEGIN
    SELECT * INTO R FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID FOR UPDATE NOWAIT;
    V_OLD_ST := R.STATUS;
    IF R.STATUS NOT IN (C_REQ_STATUS_DRAFT, C_REQ_STATUS_PARTIAL_APPROVED, C_REQ_STATUS_PARTIAL_PAID) THEN
      P_MSG_OUT := 'لا يمكن الاعتماد بهذه الحالة'; RETURN;
    END IF;
    V_USER := USER_PKG.GET_USER_ID;

    FOR D IN (SELECT * FROM GFC.PAYMENT_REQ_DETAIL_TB
               WHERE REQ_ID = P_REQ_ID AND DETAIL_STATUS = C_REQ_STATUS_DRAFT
               ORDER BY DETAIL_ID) LOOP
      V_MSG := NULL;
      PREPARE_REQUEST_VALUES(D.EMP_NO, R.THE_MONTH, R.REQ_TYPE,
          R.CALC_METHOD, R.PERCENT_VAL, D.REQ_AMOUNT, D.PAY_TYPE,
          D.DUES_AMOUNT, P_REQ_ID, R.L_VALUE, R.H_VALUE,
          V_MONTH, V_AMT, V_PT, V_DA, V_LF, V_MSG);
      IF V_MSG IS NOT NULL THEN
        V_ERR := V_ERR + 1;
        IF LENGTH(V_FAIL_LIST) < 3500 THEN
          V_FAIL_LIST := V_FAIL_LIST || D.EMP_NO || ': ' || V_MSG || CHR(10);
        END IF;
      ELSE
        UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET
            REQ_AMOUNT = V_AMT, PAY_TYPE = V_PT, DUES_AMOUNT = V_DA,
            LIMIT_FLAG = V_LF, DETAIL_STATUS = C_REQ_STATUS_APPROVED
         WHERE DETAIL_ID = D.DETAIL_ID;
        V_OK := V_OK + 1;
      END IF;
    END LOOP;

    IF V_OK = 0 AND V_ERR = 0 THEN P_MSG_OUT := 'لا يوجد موظفين بحاجة اعتماد'; RETURN; END IF;

    V_NEW_ST := CALC_MASTER_STATUS(P_REQ_ID);
    UPDATE GFC.PAYMENT_REQ_TB SET
        STATUS = V_NEW_ST,
        APPROVE_USER = CASE WHEN V_NEW_ST IN (C_REQ_STATUS_APPROVED, C_REQ_STATUS_PARTIAL_APPROVED) THEN V_USER ELSE APPROVE_USER END,
        APPROVE_DATE = CASE WHEN V_NEW_ST IN (C_REQ_STATUS_APPROVED, C_REQ_STATUS_PARTIAL_APPROVED) THEN SYSDATE ELSE APPROVE_DATE END,
        UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE
     WHERE REQ_ID = P_REQ_ID;

    SYNC_MASTER_AMOUNT(P_REQ_ID);
    LOG_ACTION(P_REQ_ID, 'APPROVE_ALL', V_OLD_ST, V_NEW_ST, 'OK=' || V_OK || ' ERR=' || V_ERR);

    IF V_ERR > 0 AND V_OK > 0 THEN
      P_MSG_OUT := '1|تم اعتماد ' || V_OK || ' — فشل ' || V_ERR || ':' || CHR(10) || V_FAIL_LIST;
    ELSIF V_ERR > 0 THEN
      P_MSG_OUT := 'فشل اعتماد ' || V_ERR || ' موظف:' || CHR(10) || V_FAIL_LIST;
    ELSE
      P_MSG_OUT := '1';
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_PAY (P_REQ_ID NUMBER, P_MSG_OUT OUT VARCHAR2) IS
