              AND (P_REQ_IDS IS NULL OR INSTR(','||P_REQ_IDS||',', ','||TO_CHAR(REQ_ID)||',') > 0)
              ORDER BY REQ_ID)
    LOOP
      BEGIN
        V_MSG := NULL;
        PAYMENT_REQ_PAY(R.REQ_ID, V_MSG);
        IF V_MSG = '1' THEN V_OK := V_OK + 1;
        ELSE V_ERR := V_ERR + 1;
          BEGIN INSERT INTO GFC.PROCESS_LOG_TB (LOG_ID,EMP_NO,THE_MONTH,PROCESS_NAME,ERROR_MSG,LOG_DATE)
                VALUES (PROCESS_LOG_SEQ.NEXTVAL,0,P_THE_MONTH,'PAY_BATCH',SUBSTR(V_MSG,1,500),SYSDATE);
          EXCEPTION WHEN OTHERS THEN NULL; END;
        END IF;
      EXCEPTION WHEN OTHERS THEN V_ERR := V_ERR + 1; END;
    END LOOP;
    P_MSG_OUT := TO_CHAR(V_OK) || '|' || TO_CHAR(V_ERR);
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := '0|1|' || SUBSTR(SQLERRM,1,200);
  END;


  -- ============================================================
  -- Bulk Preview / Create (1 master + N details)
  -- ============================================================

  PROCEDURE PAYMENT_REQ_BULK_PREVIEW (
      P_THE_MONTH NUMBER, P_REQ_TYPE NUMBER,
      P_CALC_METHOD NUMBER, P_PERCENT_VAL NUMBER, P_REQ_AMOUNT NUMBER,
      P_PAY_TYPE NUMBER, P_SAL_FROM NUMBER, P_SAL_TO NUMBER,
      P_BRANCH_NO NUMBER,
      P_L_VALUE NUMBER, P_H_VALUE NUMBER,
      P_REF_CUR_OUT OUT SYS_REFCURSOR, P_MSG_OUT OUT VARCHAR2
  ) IS
    V_M NUMBER := NVL(P_THE_MONTH, GET_DEFAULT_MONTH);
    V_RA NUMBER := NULLIF(P_REQ_AMOUNT,-3);
    V_BR NUMBER := NULLIF(P_BRANCH_NO,-7);
    V_PCT NUMBER; V_LV NUMBER; V_HV NUMBER;
  BEGIN
    IF P_REQ_TYPE = C_REQ_TYPE_FULL_SALARY THEN
      OPEN P_REF_CUR_OUT FOR
        SELECT A.EMP_NO, EMP_PKG.GET_EMP_NAME(A.EMP_NO) EMP_NAME,
               EMP_PKG.GET_EMP_BRANCH_NAME(A.EMP_NO) BRANCH_NAME, A.NET_SALARY,
               NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                    WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) AS CON_323_AMOUNT,
               LEAST(
                 NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                      WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0),
                 GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL)
               ) AS CALC_AMOUNT,
               (SELECT COUNT(1) FROM GFC.PAYMENT_REQ_DETAIL_TB D
                  JOIN GFC.PAYMENT_REQ_TB R ON R.REQ_ID = D.REQ_ID
                 WHERE D.EMP_NO = A.EMP_NO AND R.THE_MONTH = V_M
                   AND R.REQ_TYPE = P_REQ_TYPE AND R.STATUS <> C_REQ_STATUS_CANCELLED) AS HAS_EXISTING,
               CAST(NULL AS VARCHAR2(3)) AS LIMIT_FLAG,
               0 AS ALREADY_POSTED,
               GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL) AS SALARY_AVAILABLE,
               1 AS IS_CALCULATED,
               CASE
                 WHEN NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                      WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) <= 0 THEN
                   'لا يوجد مبلغ مستحقات (بند 323)'
                 WHEN GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL) <= 0 THEN
                   'لا يوجد رصيد مستحقات متاح (مصروف مسبقاً)'
                 ELSE NULL
               END AS SKIP_REASON
          FROM DATA.ADMIN A
         WHERE A.MONTH = V_M
           AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
         ORDER BY A.EMP_NO;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_LUMP_SUM THEN
      -- نوع 3: كل الموظفين اللي عندهم رصيد مستحقات — نسبة أو مبلغ ثابت
      V_PCT := NVL(NULLIF(P_PERCENT_VAL, -2), 0);
      OPEN P_REF_CUR_OUT FOR
        SELECT E.NO AS EMP_NO, E.NAME AS EMP_NAME,
               EMP_PKG.GET_EMP_BRANCH_NAME(E.NO) BRANCH_NAME, 0 AS NET_SALARY,
               GET_EMP_DUES_AVAILABLE(E.NO, NULL) AS CON_323_AMOUNT,
               -- حسب طريقة الاحتساب: 1=نسبة، 2=مبلغ ثابت
               CASE WHEN NULLIF(P_CALC_METHOD, -1) = C_CALC_PERCENT AND V_PCT > 0 THEN ROUND(GET_EMP_DUES_AVAILABLE(E.NO, NULL) * V_PCT / 100, 2)
                    WHEN V_RA IS NOT NULL THEN LEAST(V_RA, GET_EMP_DUES_AVAILABLE(E.NO, NULL))
                    ELSE GET_EMP_DUES_AVAILABLE(E.NO, NULL) END AS CALC_AMOUNT,
               (SELECT COUNT(1) FROM GFC.PAYMENT_REQ_DETAIL_TB D
                  JOIN GFC.PAYMENT_REQ_TB R ON R.REQ_ID = D.REQ_ID
                 WHERE D.EMP_NO = E.NO AND R.THE_MONTH = V_M
                   AND R.REQ_TYPE = P_REQ_TYPE AND R.STATUS <> C_REQ_STATUS_CANCELLED) AS HAS_EXISTING,
               CAST(NULL AS VARCHAR2(3)) AS LIMIT_FLAG,
               0 AS ALREADY_POSTED,
               GET_EMP_DUES_AVAILABLE(E.NO, NULL) AS SALARY_AVAILABLE,
               CASE WHEN E.IS_ACTIVE = 1 THEN 1 ELSE 0 END AS IS_CALCULATED,
               CASE WHEN GET_EMP_DUES_AVAILABLE(E.NO, NULL) <= 0 THEN 'رصيد المستحقات صفر'
                    ELSE NULL END AS SKIP_REASON
          FROM DATA.EMPLOYEES E
         WHERE (GET_EMP_DUES_BASE(E.NO) + GET_EMP_DUES_ADD(E.NO) - GET_EMP_DUES_DED(E.NO)) > 0
           AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(E.NO) = V_BR)
         ORDER BY E.NO;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN
      V_PCT := NVL(NULLIF(P_PERCENT_VAL, -2), 100);
      V_LV  := NULLIF(P_L_VALUE, -8);
      V_HV  := NULLIF(P_H_VALUE, -9);

      OPEN P_REF_CUR_OUT FOR
        SELECT A.EMP_NO, EMP_PKG.GET_EMP_NAME(A.EMP_NO) EMP_NAME,
               EMP_PKG.GET_EMP_BRANCH_NAME(A.EMP_NO) BRANCH_NAME, A.NET_SALARY,
               NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                    WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) AS CON_323_AMOUNT,
               -- حساب المبلغ مع الحدود
               LEAST(
                 GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL),
                 CASE
                   -- النسبة أقل من الحد الأدنى
                   WHEN V_LV IS NOT NULL
                     AND ROUND(NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                          WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) * V_PCT / 100, 2) < V_LV
                   THEN LEAST(V_LV, NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                          WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0))
                   -- النسبة أكبر من الحد الأعلى
                   WHEN V_HV IS NOT NULL
                     AND ROUND(NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                          WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) * V_PCT / 100, 2) > V_HV
                   THEN V_HV
                   -- ضمن الحدود
                   ELSE ROUND(NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                          WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) * V_PCT / 100, 2)
                 END
               ) AS CALC_AMOUNT,
               (SELECT COUNT(1) FROM GFC.PAYMENT_REQ_DETAIL_TB D
                  JOIN GFC.PAYMENT_REQ_TB R ON R.REQ_ID = D.REQ_ID
                 WHERE D.EMP_NO = A.EMP_NO AND R.THE_MONTH = V_M
                   AND R.REQ_TYPE = P_REQ_TYPE AND R.STATUS <> C_REQ_STATUS_CANCELLED) AS HAS_EXISTING,
               -- LIMIT_FLAG للمعاينة
               CASE
                 WHEN V_LV IS NOT NULL
                   AND ROUND(NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                        WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) * V_PCT / 100, 2) < V_LV
                   AND NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                        WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) < V_LV
                 THEN 'CAP'
                 WHEN V_LV IS NOT NULL
                   AND ROUND(NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                        WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) * V_PCT / 100, 2) < V_LV
                 THEN 'MIN'
                 WHEN V_HV IS NOT NULL
                   AND ROUND(NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                        WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) * V_PCT / 100, 2) > V_HV
                 THEN 'MAX'
                 ELSE NULL
               END AS LIMIT_FLAG,
               1 AS ALREADY_POSTED,
               GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL) AS SALARY_AVAILABLE,
               1 AS IS_CALCULATED,
               CASE
                 WHEN NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                      WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) <= 0 THEN
                   'لا يوجد مستحقات (بند 323)'
                 WHEN GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL) <= 0 THEN
                   'لا يوجد رصيد مستحقات متاح'
                 ELSE NULL
               END AS SKIP_REASON
          FROM DATA.ADMIN A
         WHERE A.MONTH = V_M
           AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
         ORDER BY A.EMP_NO;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_MONTHLY_DUES THEN
      -- نوع 4: المتبقي من بند 323 للشهر
      OPEN P_REF_CUR_OUT FOR
        SELECT A.EMP_NO, EMP_PKG.GET_EMP_NAME(A.EMP_NO) EMP_NAME,
               EMP_PKG.GET_EMP_BRANCH_NAME(A.EMP_NO) BRANCH_NAME, A.NET_SALARY,
               NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                    WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) AS CON_323_AMOUNT,
               -- المتبقي = بند 323 - المصروف من 1,2,4
               GREATEST(
                 NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                      WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0)
                 - NVL((SELECT SUM(DD.REQ_AMOUNT) FROM GFC.PAYMENT_REQ_DETAIL_TB DD
                        JOIN GFC.PAYMENT_REQ_TB MM ON MM.REQ_ID = DD.REQ_ID
                       WHERE DD.EMP_NO = A.EMP_NO AND MM.THE_MONTH = V_M
                         AND MM.REQ_TYPE IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_MONTHLY_DUES)
                         AND MM.STATUS <> C_REQ_STATUS_CANCELLED
                         AND DD.DETAIL_STATUS <> C_REQ_STATUS_CANCELLED), 0),
                 0
               ) AS CALC_AMOUNT,
               (SELECT COUNT(1) FROM GFC.PAYMENT_REQ_DETAIL_TB D
                  JOIN GFC.PAYMENT_REQ_TB R ON R.REQ_ID = D.REQ_ID
                 WHERE D.EMP_NO = A.EMP_NO AND R.THE_MONTH = V_M
                   AND R.REQ_TYPE = P_REQ_TYPE AND R.STATUS <> C_REQ_STATUS_CANCELLED) AS HAS_EXISTING,
               CAST(NULL AS VARCHAR2(3)) AS LIMIT_FLAG,
               1 AS ALREADY_POSTED,
               GET_EMP_DUES_AVAILABLE(A.EMP_NO, NULL) AS SALARY_AVAILABLE,
               1 AS IS_CALCULATED,
               CASE
                 WHEN NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                      WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) <= 0 THEN
                   'لا يوجد مستحقات (بند 323)'
                 WHEN NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
                      WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0)
                      - NVL((SELECT SUM(DD.REQ_AMOUNT) FROM GFC.PAYMENT_REQ_DETAIL_TB DD
                             JOIN GFC.PAYMENT_REQ_TB MM ON MM.REQ_ID = DD.REQ_ID
                            WHERE DD.EMP_NO = A.EMP_NO AND MM.THE_MONTH = V_M
                              AND MM.REQ_TYPE IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_MONTHLY_DUES)
                              AND MM.STATUS <> C_REQ_STATUS_CANCELLED
                              AND DD.DETAIL_STATUS <> C_REQ_STATUS_CANCELLED), 0) <= 0 THEN
                   'تم صرف كامل بند 323'
                 ELSE NULL
               END AS SKIP_REASON
          FROM DATA.ADMIN A
         WHERE A.MONTH = V_M
           AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
         ORDER BY A.EMP_NO;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_BENEFITS THEN
      -- نوع 5: كل الموظفين المحتسبين (صرف بدون تأثير)
      OPEN P_REF_CUR_OUT FOR
        SELECT A.EMP_NO, EMP_PKG.GET_EMP_NAME(A.EMP_NO) EMP_NAME,
               EMP_PKG.GET_EMP_BRANCH_NAME(A.EMP_NO) BRANCH_NAME, A.NET_SALARY,
               0 AS CON_323_AMOUNT,
               NVL(V_RA, 0) AS CALC_AMOUNT,
               0 AS HAS_EXISTING,
               CAST(NULL AS VARCHAR2(3)) AS LIMIT_FLAG,
               0 AS ALREADY_POSTED,
               0 AS SALARY_AVAILABLE,
               1 AS IS_CALCULATED,
               CAST(NULL AS VARCHAR2(200)) AS SKIP_REASON
          FROM DATA.ADMIN A
         WHERE A.MONTH = V_M
           AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
         ORDER BY A.EMP_NO;

    ELSE
      OPEN P_REF_CUR_OUT FOR
        SELECT NULL AS EMP_NO, NULL AS EMP_NAME, NULL AS BRANCH_NAME,
               NULL AS NET_SALARY, NULL AS CON_323_AMOUNT, NULL AS CALC_AMOUNT, NULL AS HAS_EXISTING,
               NULL AS LIMIT_FLAG, NULL AS ALREADY_POSTED, NULL AS SALARY_AVAILABLE,
               NULL AS IS_CALCULATED, NULL AS SKIP_REASON
          FROM DUAL WHERE 1=0;
    END IF;
    P_MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_BULK_CREATE (
      P_THE_MONTH NUMBER, P_REQ_TYPE NUMBER,
      P_CALC_METHOD NUMBER, P_PERCENT_VAL NUMBER, P_REQ_AMOUNT NUMBER,
      P_PAY_TYPE NUMBER, P_SAL_FROM NUMBER, P_SAL_TO NUMBER,
      P_BRANCH_NO NUMBER,
      P_L_VALUE NUMBER, P_H_VALUE NUMBER,
      P_NOTE VARCHAR2,
      P_MSG_OUT OUT VARCHAR2
  ) IS
    V_M NUMBER := NVL(P_THE_MONTH, GET_DEFAULT_MONTH);
    V_RA NUMBER := NULLIF(P_REQ_AMOUNT,-3);
    V_BR NUMBER := NULLIF(P_BRANCH_NO,-7);
    V_PT NUMBER := NULLIF(P_PAY_TYPE,-4);
    V_NT VARCHAR2(500) := NULLIF(P_NOTE,'N/A');
    V_OK NUMBER := 0; V_SK NUMBER := 0; V_ER NUMBER := 0;
    V_MASTER_ID NUMBER; V_MSG VARCHAR2(4000); V_DET_MSG VARCHAR2(4000);
  BEGIN
    IF P_REQ_TYPE NOT IN (C_REQ_TYPE_FULL_SALARY, C_REQ_TYPE_PART_SALARY, C_REQ_TYPE_LUMP_SUM, C_REQ_TYPE_MONTHLY_DUES, C_REQ_TYPE_BENEFITS) THEN
      P_MSG_OUT := '0|0|0|0'; RETURN;
    END IF;

    PAYMENT_REQ_INSERT(V_M, P_REQ_TYPE, P_CALC_METHOD, P_PERCENT_VAL, P_L_VALUE, P_H_VALUE, V_PT, NULL, NVL(V_NT,'bulk'), V_MSG);
    IF NOT (V_MSG IS NOT NULL AND REGEXP_LIKE(V_MSG, '^\d+$')) THEN
      P_MSG_OUT := '0|0|0|1|' || NVL(V_MSG,'Failed to create master');
      RETURN;
    END IF;
    V_MASTER_ID := TO_NUMBER(V_MSG);

    IF P_REQ_TYPE = C_REQ_TYPE_FULL_SALARY THEN
      FOR R IN (SELECT A.EMP_NO FROM DATA.ADMIN A
                 WHERE A.MONTH = V_M AND A.NET_SALARY = 0
                   AND EXISTS (SELECT 1 FROM DATA.SALARY S WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M AND S.VALUE > 0)
                   AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
                 ORDER BY A.EMP_NO)
      LOOP
        BEGIN
          V_DET_MSG := NULL;
          DETAIL_INSERT(V_MASTER_ID, R.EMP_NO, V_RA, NVL(V_NT,'bulk'), V_DET_MSG);
          IF V_DET_MSG IS NOT NULL AND REGEXP_LIKE(V_DET_MSG, '^\d+$') THEN V_OK := V_OK + 1;
          ELSIF INSTR(V_DET_MSG, 'موجود مسبقا') > 0 THEN V_SK := V_SK + 1;
          ELSE V_ER := V_ER + 1;
            BEGIN INSERT INTO GFC.PROCESS_LOG_TB (LOG_ID,EMP_NO,THE_MONTH,PROCESS_NAME,ERROR_MSG,LOG_DATE)
                  VALUES (PROCESS_LOG_SEQ.NEXTVAL,R.EMP_NO,V_M,'BULK_CREATE',SUBSTR(V_DET_MSG,1,500),SYSDATE);
            EXCEPTION WHEN OTHERS THEN NULL; END;
          END IF;
        EXCEPTION WHEN OTHERS THEN V_ER := V_ER + 1; END;
      END LOOP;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_PART_SALARY THEN
      FOR R IN (SELECT A.EMP_NO FROM DATA.ADMIN A
                 WHERE A.MONTH = V_M AND A.NET_SALARY = 0
                   AND EXISTS (SELECT 1 FROM DATA.SALARY S WHERE S.EMP_NO = A.EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M AND S.VALUE > 0)
                   AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
                 ORDER BY A.EMP_NO)
      LOOP
        BEGIN
          V_DET_MSG := NULL;
          DETAIL_INSERT(V_MASTER_ID, R.EMP_NO, V_RA, NVL(V_NT,'bulk'), V_DET_MSG);
          IF V_DET_MSG IS NOT NULL AND REGEXP_LIKE(V_DET_MSG, '^\d+$') THEN V_OK := V_OK + 1;
          ELSIF INSTR(V_DET_MSG, 'موجود مسبقا') > 0 THEN V_SK := V_SK + 1;
          ELSE V_ER := V_ER + 1;
            BEGIN INSERT INTO GFC.PROCESS_LOG_TB (LOG_ID,EMP_NO,THE_MONTH,PROCESS_NAME,ERROR_MSG,LOG_DATE)
                  VALUES (PROCESS_LOG_SEQ.NEXTVAL,R.EMP_NO,V_M,'BULK_CREATE',SUBSTR(V_DET_MSG,1,500),SYSDATE);
            EXCEPTION WHEN OTHERS THEN NULL; END;
          END IF;
        EXCEPTION WHEN OTHERS THEN V_ER := V_ER + 1; END;
      END LOOP;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_LUMP_SUM THEN
      FOR R IN (SELECT E.NO AS EMP_NO FROM DATA.EMPLOYEES E
                 WHERE E.IS_ACTIVE = 1 AND GET_EMP_DUES_AVAILABLE(E.NO, NULL) > 0
                   AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(E.NO) = V_BR)
                 ORDER BY E.NO)
      LOOP
        BEGIN
          V_DET_MSG := NULL;
          DETAIL_INSERT(V_MASTER_ID, R.EMP_NO, V_RA, NVL(V_NT,'bulk'), V_DET_MSG);
          IF V_DET_MSG IS NOT NULL AND REGEXP_LIKE(V_DET_MSG, '^\d+$') THEN V_OK := V_OK + 1;
          ELSIF INSTR(V_DET_MSG, 'موجود مسبقا') > 0 THEN V_SK := V_SK + 1;
          ELSE V_ER := V_ER + 1;
            BEGIN INSERT INTO GFC.PROCESS_LOG_TB (LOG_ID,EMP_NO,THE_MONTH,PROCESS_NAME,ERROR_MSG,LOG_DATE)
                  VALUES (PROCESS_LOG_SEQ.NEXTVAL,R.EMP_NO,V_M,'BULK_CREATE',SUBSTR(V_DET_MSG,1,500),SYSDATE);
            EXCEPTION WHEN OTHERS THEN NULL; END;
          END IF;
        EXCEPTION WHEN OTHERS THEN V_ER := V_ER + 1; END;
      END LOOP;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_MONTHLY_DUES THEN
      -- نوع 4: الموظفين اللي عندهم متبقي من بند 323
      FOR R IN (SELECT A.EMP_NO FROM DATA.ADMIN A
                 WHERE A.MONTH = V_M
                   AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
                 ORDER BY A.EMP_NO)
      LOOP
        BEGIN
          V_DET_MSG := NULL;
          DETAIL_INSERT(V_MASTER_ID, R.EMP_NO, NVL(V_RA, 0), NVL(V_NT,'bulk'), V_DET_MSG);
          IF V_DET_MSG IS NOT NULL AND REGEXP_LIKE(V_DET_MSG, '^\d+$') THEN V_OK := V_OK + 1;
          ELSIF INSTR(V_DET_MSG, 'موجود') > 0 OR INSTR(V_DET_MSG, 'متبقي') > 0 THEN V_SK := V_SK + 1;
          ELSE V_ER := V_ER + 1; END IF;
        EXCEPTION WHEN OTHERS THEN V_ER := V_ER + 1; END;
      END LOOP;

    ELSIF P_REQ_TYPE = C_REQ_TYPE_BENEFITS THEN
      -- نوع 5: كل الموظفين المحتسبين
      FOR R IN (SELECT A.EMP_NO FROM DATA.ADMIN A
                 WHERE A.MONTH = V_M
                   AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(A.EMP_NO) = V_BR)
                 ORDER BY A.EMP_NO)
      LOOP
        BEGIN
          V_DET_MSG := NULL;
          DETAIL_INSERT(V_MASTER_ID, R.EMP_NO, V_RA, NVL(V_NT,'bulk'), V_DET_MSG);
          IF V_DET_MSG IS NOT NULL AND REGEXP_LIKE(V_DET_MSG, '^\d+$') THEN V_OK := V_OK + 1;
          ELSIF INSTR(V_DET_MSG, 'موجود') > 0 THEN V_SK := V_SK + 1;
          ELSE V_ER := V_ER + 1; END IF;
        EXCEPTION WHEN OTHERS THEN V_ER := V_ER + 1; END;
      END LOOP;
    END IF;

    P_MSG_OUT := TO_CHAR(V_MASTER_ID) || '|' || TO_CHAR(V_OK) || '|' || TO_CHAR(V_SK) || '|' || TO_CHAR(V_ER);
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := '0|0|0|1|' || SUBSTR(SQLERRM,1,200);
  END;


  -- ============================================================
  -- Month Status
  -- ============================================================

  PROCEDURE GET_MONTH_STATUS (
      P_THE_MONTH NUMBER,
      P_REF_CUR_OUT OUT SYS_REFCURSOR, P_MSG_OUT OUT VARCHAR2
  ) IS
    V_M NUMBER := NVL(P_THE_MONTH, GET_DEFAULT_MONTH);
    V_CNT NUMBER := 0;
  BEGIN
    SELECT COUNT(1) INTO V_CNT FROM DATA.ADMIN WHERE MONTH = V_M AND ROWNUM = 1;

    OPEN P_REF_CUR_OUT FOR
      SELECT
        CASE WHEN V_CNT > 0 THEN 'admin' ELSE 'none' END AS SOURCE,
        NVL((SELECT COUNT(1) FROM DATA.ADMIN WHERE MONTH = V_M), 0) AS EMP_COUNT,
        NVL((SELECT SUM(NET_SALARY) FROM DATA.ADMIN WHERE MONTH = V_M), 0) AS TOTAL_NET,
        NVL((SELECT SUM(S.VALUE) FROM DATA.SALARY S
              WHERE S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_M), 0) AS TOTAL_323,
        NVL((SELECT SUM(D.DUES_AMOUNT) FROM GFC.PAYMENT_REQ_DETAIL_TB D
               JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
              WHERE M.THE_MONTH = V_M
                AND D.DETAIL_STATUS = C_REQ_STATUS_DRAFT
                AND M.STATUS <> C_REQ_STATUS_CANCELLED), 0) AS TOTAL_DRAFT,
        NVL((SELECT SUM(D.DUES_AMOUNT) FROM GFC.PAYMENT_REQ_DETAIL_TB D
               JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
              WHERE M.THE_MONTH = V_M
                AND D.DETAIL_STATUS = C_REQ_STATUS_APPROVED
                AND M.STATUS <> C_REQ_STATUS_CANCELLED), 0) AS TOTAL_APPROVED,
        NVL((SELECT SUM(D.DUES_AMOUNT) FROM GFC.PAYMENT_REQ_DETAIL_TB D
               JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
              WHERE M.THE_MONTH = V_M
                AND D.DETAIL_STATUS = C_REQ_STATUS_PAID
                AND M.STATUS <> C_REQ_STATUS_CANCELLED), 0) AS TOTAL_PAID,
        (SELECT COUNT(DISTINCT M.REQ_ID) FROM GFC.PAYMENT_REQ_TB M
           JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.REQ_ID = M.REQ_ID
          WHERE M.THE_MONTH = V_M
            AND M.STATUS <> C_REQ_STATUS_CANCELLED) AS REQ_COUNT
      FROM DUAL;

    P_MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  -- ============================================================
  -- تقرير شهري
  -- ============================================================
  PROCEDURE PAYMENT_REQ_REPORT (
      P_MONTH_FROM NUMBER, P_MONTH_TO NUMBER DEFAULT NULL,
      P_BRANCH_NO NUMBER DEFAULT NULL, P_REQ_TYPE NUMBER DEFAULT NULL, P_STATUS NUMBER DEFAULT NULL,
      P_REF_CUR_OUT OUT SYS_REFCURSOR, P_MSG_OUT OUT VARCHAR2
  ) IS
    V_MF NUMBER := P_MONTH_FROM;
    V_MT NUMBER := NVL(P_MONTH_TO, P_MONTH_FROM);
    V_BR NUMBER := NULLIF(P_BRANCH_NO, -1);
    V_RT NUMBER := NULLIF(P_REQ_TYPE, -1);
    V_ST NUMBER := NULLIF(P_STATUS, -1);
  BEGIN
    OPEN P_REF_CUR_OUT FOR
      SELECT M.REQ_ID, M.REQ_NO, M.THE_MONTH, M.REQ_TYPE, M.STATUS,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(538, M.REQ_TYPE) AS REQ_TYPE_NAME,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(541, M.STATUS)   AS STATUS_NAME,
             D.DETAIL_ID, D.EMP_NO,
             EMP_PKG.GET_EMP_NAME(D.EMP_NO)        AS EMP_NAME,
             EMP_PKG.GET_EMP_BRANCH_NAME(D.EMP_NO) AS BRANCH_NAME,
             EMP_PKG.GET_EMP_BRANCH(D.EMP_NO)      AS BRANCH_NO,
             D.REQ_AMOUNT, D.DUES_AMOUNT, D.DETAIL_STATUS,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(541, D.DETAIL_STATUS) AS DETAIL_STATUS_NAME,
             M.PAY_DATE, M.ENTRY_DATE
        FROM GFC.PAYMENT_REQ_TB M
        JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.REQ_ID = M.REQ_ID
       WHERE M.THE_MONTH BETWEEN V_MF AND V_MT
         AND (V_BR IS NULL OR EMP_PKG.GET_EMP_BRANCH(D.EMP_NO) = V_BR)
         AND (V_RT IS NULL OR M.REQ_TYPE = V_RT)
         AND (V_ST IS NULL OR M.STATUS = V_ST)
       ORDER BY M.THE_MONTH, M.REQ_ID, D.EMP_NO;
    P_MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  -- ============================================================
  -- كشف حساب موظف
  -- ============================================================
  PROCEDURE EMP_STATEMENT (
      P_EMP_NO NUMBER, P_MONTH_FROM NUMBER DEFAULT NULL, P_MONTH_TO NUMBER DEFAULT NULL,
      P_REF_CUR_OUT OUT SYS_REFCURSOR, P_MSG_OUT OUT VARCHAR2
  ) IS
  BEGIN
    OPEN P_REF_CUR_OUT FOR
      SELECT M.REQ_ID, M.REQ_NO, M.THE_MONTH, M.REQ_TYPE, M.STATUS,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(538, M.REQ_TYPE) AS REQ_TYPE_NAME,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(541, M.STATUS)   AS STATUS_NAME,
             D.DETAIL_ID, D.REQ_AMOUNT, D.DUES_AMOUNT, D.DETAIL_STATUS,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(541, D.DETAIL_STATUS) AS DETAIL_STATUS_NAME,
             D.PAY_TYPE,
             GET_DUES_TYPE_NAME(D.PAY_TYPE) AS PAY_TYPE_NAME,
             D.POST_SERIAL_DUES, D.NOTE,
             M.PAY_DATE, M.ENTRY_DATE
        FROM GFC.PAYMENT_REQ_TB M
        JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.REQ_ID = M.REQ_ID
       WHERE D.EMP_NO = P_EMP_NO
         AND M.STATUS <> C_REQ_STATUS_CANCELLED
         AND (P_MONTH_FROM IS NULL OR M.THE_MONTH >= P_MONTH_FROM)
         AND (P_MONTH_TO   IS NULL OR M.THE_MONTH <= P_MONTH_TO)
       ORDER BY M.THE_MONTH DESC, M.REQ_ID DESC;
    P_MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;



