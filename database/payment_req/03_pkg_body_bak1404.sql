  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_CANCEL (P_REQ_ID NUMBER, P_CANCEL_NOTE VARCHAR2 DEFAULT 'Cancelled', P_MSG_OUT OUT VARCHAR2) IS
    V_ST NUMBER; V_USER NUMBER; V_BATCH_CNT NUMBER;
    V_REVERSED NUMBER := 0; V_DETAIL_NOTE VARCHAR2(4000) := '';
  BEGIN
    SELECT STATUS INTO V_ST FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID FOR UPDATE NOWAIT;
    IF V_ST = C_REQ_STATUS_CANCELLED THEN P_MSG_OUT := 'الطلب ملغي مسبقا'; RETURN; END IF;

    -- فحص: مش محتسب بدفعة نشطة
    SELECT COUNT(1) INTO V_BATCH_CNT
      FROM GFC.PAYMENT_BATCH_LINK_TB L
      JOIN GFC.PAYMENT_BATCH_TB B ON B.BATCH_ID = L.BATCH_ID
      JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.DETAIL_ID = L.DETAIL_ID
     WHERE D.REQ_ID = P_REQ_ID AND B.STATUS <> 9;
    IF V_BATCH_CNT > 0 THEN P_MSG_OUT := 'الطلب محتسب ضمن دفعة — يجب فك الاحتساب أولاً'; RETURN; END IF;

    V_USER := USER_PKG.GET_USER_ID;

    -- عكس المصروف من دفتر المستحقات (كل ديتيل فيه قيد فعّال)
    FOR D IN (SELECT D.DETAIL_ID, D.EMP_NO, D.POST_SERIAL_DUES, D.DETAIL_STATUS, D.REQ_AMOUNT
                FROM GFC.PAYMENT_REQ_DETAIL_TB D
               WHERE D.REQ_ID = P_REQ_ID
                 AND D.POST_SERIAL_DUES IS NOT NULL
                 AND D.DETAIL_STATUS = C_REQ_STATUS_PAID)
    LOOP
      UPDATE GFC.SALARY_DUES_TB SET STATUS = 0, CANCEL_USER = V_USER, CANCEL_DATE = SYSDATE,
          CANCEL_NOTE = SUBSTR(P_CANCEL_NOTE, 1, 200)
       WHERE SERIAL = D.POST_SERIAL_DUES AND NVL(STATUS,1) = 1;
      IF SQL%ROWCOUNT > 0 THEN
        V_REVERSED := V_REVERSED + 1;
        V_DETAIL_NOTE := V_DETAIL_NOTE || 'EMP=' || D.EMP_NO || ' SER=' || D.POST_SERIAL_DUES || ' AMT=' || D.REQ_AMOUNT || '; ';
      END IF;
    END LOOP;

    -- إلغاء كل الديتيل + مسح POST_SERIAL_DUES
    UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET
        DETAIL_STATUS = C_REQ_STATUS_CANCELLED,
        POST_SERIAL_DUES = NULL
     WHERE REQ_ID = P_REQ_ID
       AND DETAIL_STATUS <> C_REQ_STATUS_CANCELLED;

    UPDATE GFC.PAYMENT_REQ_TB SET
        STATUS = C_REQ_STATUS_CANCELLED,
        CANCEL_USER = V_USER, CANCEL_DATE = SYSDATE,
        CANCEL_NOTE = SUBSTR(P_CANCEL_NOTE, 1, 200),
        UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE
     WHERE REQ_ID = P_REQ_ID;

    SYNC_MASTER_AMOUNT(P_REQ_ID);
    LOG_ACTION(P_REQ_ID, 'CANCEL', V_ST, C_REQ_STATUS_CANCELLED,
        P_CANCEL_NOTE || CASE WHEN V_REVERSED > 0 THEN ' | عكس ' || V_REVERSED || ' قيد: ' || SUBSTR(V_DETAIL_NOTE, 1, 300) END);
    P_MSG_OUT := '1';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE PAYMENT_REQ_DELETE (P_REQ_ID NUMBER, P_MSG_OUT OUT VARCHAR2) IS
    V_ST NUMBER; V_CNT NUMBER; V_BATCH_CNT NUMBER;
  BEGIN
    SELECT STATUS INTO V_ST FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID FOR UPDATE NOWAIT;

    IF V_ST IN (C_REQ_STATUS_PAID, C_REQ_STATUS_PARTIAL_PAID) THEN
      P_MSG_OUT := 'لا يمكن حذف طلب فيه مصروفين — استخدم الإلغاء';
      RETURN;
    END IF;

    -- فحص: مش محتسب بدفعة نشطة
    SELECT COUNT(1) INTO V_BATCH_CNT
      FROM GFC.PAYMENT_BATCH_LINK_TB L
      JOIN GFC.PAYMENT_BATCH_TB B ON B.BATCH_ID = L.BATCH_ID
      JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.DETAIL_ID = L.DETAIL_ID
     WHERE D.REQ_ID = P_REQ_ID AND B.STATUS <> 9;
    IF V_BATCH_CNT > 0 THEN P_MSG_OUT := 'الطلب محتسب ضمن دفعة — يجب فك الاحتساب أولاً'; RETURN; END IF;

    LOG_ACTION(P_REQ_ID, 'DELETE', V_ST, NULL);

    -- حذف ربط الدفعات (الملغية) قبل حذف الديتيل
    DELETE FROM GFC.PAYMENT_BATCH_LINK_TB WHERE DETAIL_ID IN (
        SELECT DETAIL_ID FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID
    );
    DELETE FROM GFC.PAYMENT_REQ_LOG_TB WHERE REQ_ID = P_REQ_ID;
    DELETE FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE REQ_ID = P_REQ_ID;
    V_CNT := SQL%ROWCOUNT;
    DELETE FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID;

    P_MSG_OUT := '1|' || TO_CHAR(V_CNT);
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


