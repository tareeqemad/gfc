  PROCEDURE DETAIL_DELETE (P_DETAIL_ID NUMBER, P_MSG_OUT OUT VARCHAR2) IS
    V_REQ_ID NUMBER; V_EMP_NO NUMBER; V_DS NUMBER; V_MS NUMBER;
    V_USER NUMBER; V_SERIAL NUMBER; V_NEW_ST NUMBER;
  BEGIN
    SELECT D.REQ_ID, D.EMP_NO, D.DETAIL_STATUS, D.POST_SERIAL_DUES, M.STATUS
      INTO V_REQ_ID, V_EMP_NO, V_DS, V_SERIAL, V_MS
      FROM GFC.PAYMENT_REQ_DETAIL_TB D
      JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
     WHERE D.DETAIL_ID = P_DETAIL_ID;

    -- الملغى مسبقاً
    IF V_DS = C_REQ_STATUS_CANCELLED THEN P_MSG_OUT := 'الموظف ملغى مسبقاً'; RETURN; END IF;

    -- الماستر ملغى بالكامل
    IF V_MS = C_REQ_STATUS_CANCELLED THEN P_MSG_OUT := 'الطلب ملغى'; RETURN; END IF;

    V_USER := USER_PKG.GET_USER_ID;

    -- مسودة: حذف فعلي
    IF V_DS = C_REQ_STATUS_DRAFT THEN
      DELETE FROM GFC.PAYMENT_REQ_DETAIL_TB WHERE DETAIL_ID = P_DETAIL_ID;
      SYNC_MASTER_AMOUNT(V_REQ_ID);
      V_NEW_ST := CALC_MASTER_STATUS(V_REQ_ID);
      UPDATE GFC.PAYMENT_REQ_TB SET STATUS = V_NEW_ST, UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE WHERE REQ_ID = V_REQ_ID;
      LOG_ACTION(V_REQ_ID, 'REMOVE_EMP', V_MS, V_NEW_ST, 'EMP_NO=' || TO_CHAR(V_EMP_NO) || ' (حذف)');
      P_MSG_OUT := '1';
      RETURN;
    END IF;

    -- معتمد: إلغاء الاعتماد → يرجع مسودة
    IF V_DS = C_REQ_STATUS_APPROVED THEN
      UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET DETAIL_STATUS = C_REQ_STATUS_DRAFT WHERE DETAIL_ID = P_DETAIL_ID;
      V_NEW_ST := CALC_MASTER_STATUS(V_REQ_ID);
      UPDATE GFC.PAYMENT_REQ_TB SET STATUS = V_NEW_ST, UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE WHERE REQ_ID = V_REQ_ID;
      LOG_ACTION(V_REQ_ID, 'UNAPPROVE_EMP', V_MS, V_NEW_ST, 'EMP_NO=' || TO_CHAR(V_EMP_NO));
      P_MSG_OUT := '1';
      RETURN;
    END IF;

    -- مصروف: عكس القيد من دفتر المستحقات + إرجاع لمعتمد
    IF V_DS = C_REQ_STATUS_PAID THEN
      IF V_SERIAL IS NOT NULL THEN
        UPDATE GFC.SALARY_DUES_TB SET STATUS = 0, CANCEL_USER = V_USER, CANCEL_DATE = SYSDATE,
            CANCEL_NOTE = 'عكس من طلب ' || TO_CHAR(V_REQ_ID)
         WHERE SERIAL = V_SERIAL AND NVL(STATUS,1) = 1;
      END IF;
      UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET DETAIL_STATUS = C_REQ_STATUS_APPROVED, POST_SERIAL_DUES = NULL
       WHERE DETAIL_ID = P_DETAIL_ID;
      V_NEW_ST := CALC_MASTER_STATUS(V_REQ_ID);
      UPDATE GFC.PAYMENT_REQ_TB SET STATUS = V_NEW_ST, UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE WHERE REQ_ID = V_REQ_ID;
      LOG_ACTION(V_REQ_ID, 'UNPAY_EMP', V_MS, V_NEW_ST, 'EMP_NO=' || TO_CHAR(V_EMP_NO) || ' SERIAL=' || TO_CHAR(V_SERIAL));
      P_MSG_OUT := '1';
      RETURN;
    END IF;

    P_MSG_OUT := 'حالة غير معروفة';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'السجل غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  -- ============================================================
  -- صرف جزئي: اعتماد موظفين محددين
  -- ============================================================
  PROCEDURE DETAIL_APPROVE (P_DETAIL_IDS VARCHAR2, P_MSG_OUT OUT VARCHAR2) IS
    R GFC.PAYMENT_REQ_TB%ROWTYPE;
    V_REQ_ID NUMBER := NULL; V_USER NUMBER;
    V_MONTH NUMBER; V_AMT NUMBER; V_PT NUMBER; V_DA NUMBER; V_LF VARCHAR2(3); V_MSG VARCHAR2(4000);
    V_OK NUMBER := 0; V_ERR NUMBER := 0; V_FAIL_LIST VARCHAR2(4000) := '';
    V_OLD_ST NUMBER; V_NEW_ST NUMBER;
  BEGIN
    V_USER := USER_PKG.GET_USER_ID;

    FOR D IN (SELECT * FROM GFC.PAYMENT_REQ_DETAIL_TB
               WHERE INSTR(',' || P_DETAIL_IDS || ',', ',' || TO_CHAR(DETAIL_ID) || ',') > 0
               ORDER BY DETAIL_ID)
    LOOP
      -- تحقق أول مرة: كل الديتيل لازم من نفس الطلب
      IF V_REQ_ID IS NULL THEN
        V_REQ_ID := D.REQ_ID;
        SELECT * INTO R FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = V_REQ_ID FOR UPDATE NOWAIT;
        V_OLD_ST := R.STATUS;
        IF R.STATUS NOT IN (C_REQ_STATUS_DRAFT, C_REQ_STATUS_PARTIAL_APPROVED, C_REQ_STATUS_PARTIAL_PAID) THEN
          P_MSG_OUT := 'لا يمكن الاعتماد بهذه الحالة'; RETURN;
        END IF;
      ELSIF D.REQ_ID <> V_REQ_ID THEN
        P_MSG_OUT := 'كل الموظفين لازم من نفس الطلب'; RETURN;
      END IF;

      IF D.DETAIL_STATUS <> C_REQ_STATUS_DRAFT THEN
        V_ERR := V_ERR + 1;
        V_FAIL_LIST := V_FAIL_LIST || D.EMP_NO || ' - ' || EMP_PKG.GET_EMP_NAME(D.EMP_NO) || ': حالة غير مسودة' || CHR(10);
      ELSE
        V_MSG := NULL;
        PREPARE_REQUEST_VALUES(D.EMP_NO, R.THE_MONTH, R.REQ_TYPE,
            R.CALC_METHOD, R.PERCENT_VAL, D.REQ_AMOUNT, D.PAY_TYPE,
            D.DUES_AMOUNT, V_REQ_ID, R.L_VALUE, R.H_VALUE,
            V_MONTH, V_AMT, V_PT, V_DA, V_LF, V_MSG);
        IF V_MSG IS NOT NULL THEN
          V_ERR := V_ERR + 1;
          V_FAIL_LIST := V_FAIL_LIST || D.EMP_NO || ' - ' || EMP_PKG.GET_EMP_NAME(D.EMP_NO) || ': ' || V_MSG || CHR(10);
        ELSE
          UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET
              REQ_AMOUNT = V_AMT, PAY_TYPE = V_PT, DUES_AMOUNT = V_DA,
              LIMIT_FLAG = V_LF, DETAIL_STATUS = C_REQ_STATUS_APPROVED
           WHERE DETAIL_ID = D.DETAIL_ID;
          V_OK := V_OK + 1;
        END IF;
      END IF;
    END LOOP;

    IF V_REQ_ID IS NULL THEN P_MSG_OUT := 'لم يتم العثور على سجلات'; RETURN; END IF;

    -- تحديث حالة الماستر
    V_NEW_ST := CALC_MASTER_STATUS(V_REQ_ID);
    UPDATE GFC.PAYMENT_REQ_TB SET
        STATUS = V_NEW_ST,
        APPROVE_USER = CASE WHEN V_NEW_ST IN (C_REQ_STATUS_APPROVED, C_REQ_STATUS_PARTIAL_APPROVED) THEN V_USER ELSE APPROVE_USER END,
        APPROVE_DATE = CASE WHEN V_NEW_ST IN (C_REQ_STATUS_APPROVED, C_REQ_STATUS_PARTIAL_APPROVED) THEN SYSDATE ELSE APPROVE_DATE END,
        UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE
     WHERE REQ_ID = V_REQ_ID;

    SYNC_MASTER_AMOUNT(V_REQ_ID);
    LOG_ACTION(V_REQ_ID, 'APPROVE', V_OLD_ST, V_NEW_ST, 'OK=' || V_OK || ' ERR=' || V_ERR);

    IF V_ERR > 0 AND V_OK > 0 THEN
      P_MSG_OUT := '1|تم اعتماد ' || V_OK || ' موظف - فشل ' || V_ERR || ':' || CHR(10) || SUBSTR(V_FAIL_LIST, 1, 3000);
    ELSIF V_ERR > 0 THEN
      P_MSG_OUT := 'فشل اعتماد ' || V_ERR || ' موظف:' || CHR(10) || SUBSTR(V_FAIL_LIST, 1, 3500);
    ELSE
      P_MSG_OUT := '1';
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  -- ============================================================
  -- صرف جزئي: صرف موظفين محددين
  -- ============================================================
  PROCEDURE DETAIL_PAY (P_DETAIL_IDS VARCHAR2, P_MSG_OUT OUT VARCHAR2) IS
    R GFC.PAYMENT_REQ_TB%ROWTYPE;
    V_REQ_ID NUMBER := NULL; V_USER NUMBER; V_DUES_SERIAL NUMBER; V_LF VARCHAR2(3);
    V_MONTH NUMBER; V_AMT NUMBER; V_PT NUMBER; V_DA NUMBER; V_MSG VARCHAR2(4000);
    V_OK NUMBER := 0; V_ERR NUMBER := 0; V_FAIL_LIST VARCHAR2(4000) := '';
    V_OLD_ST NUMBER; V_NEW_ST NUMBER;
  BEGIN
    V_USER := USER_PKG.GET_USER_ID;

    FOR D IN (SELECT * FROM GFC.PAYMENT_REQ_DETAIL_TB
               WHERE INSTR(',' || P_DETAIL_IDS || ',', ',' || TO_CHAR(DETAIL_ID) || ',') > 0
               ORDER BY DETAIL_ID)
    LOOP
      IF V_REQ_ID IS NULL THEN
        V_REQ_ID := D.REQ_ID;
        SELECT * INTO R FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = V_REQ_ID FOR UPDATE NOWAIT;
        V_OLD_ST := R.STATUS;
        IF R.STATUS NOT IN (C_REQ_STATUS_APPROVED, C_REQ_STATUS_PARTIAL_APPROVED, C_REQ_STATUS_PARTIAL_PAID) THEN
          P_MSG_OUT := 'الصرف للمعتمد فقط'; RETURN;
        END IF;
      ELSIF D.REQ_ID <> V_REQ_ID THEN
        P_MSG_OUT := 'كل الموظفين لازم من نفس الطلب'; RETURN;
      END IF;

      IF D.DETAIL_STATUS <> C_REQ_STATUS_APPROVED THEN
        V_ERR := V_ERR + 1;
        V_FAIL_LIST := V_FAIL_LIST || D.EMP_NO || ' - ' || EMP_PKG.GET_EMP_NAME(D.EMP_NO) || ': حالة غير معتمد' || CHR(10);
      ELSE
        V_MSG := NULL;
        PREPARE_REQUEST_VALUES(D.EMP_NO, R.THE_MONTH, R.REQ_TYPE,
            R.CALC_METHOD, R.PERCENT_VAL, D.REQ_AMOUNT, D.PAY_TYPE,
            D.DUES_AMOUNT, V_REQ_ID, R.L_VALUE, R.H_VALUE,
            V_MONTH, V_AMT, V_PT, V_DA, V_LF, V_MSG);
        IF V_MSG IS NOT NULL THEN
          V_ERR := V_ERR + 1;
          V_FAIL_LIST := V_FAIL_LIST || D.EMP_NO || ' - ' || EMP_PKG.GET_EMP_NAME(D.EMP_NO) || ': ' || V_MSG || CHR(10);
        ELSE
          -- نوع 5: بدون ترحيل لجدول المستحقات
          IF R.REQ_TYPE <> C_REQ_TYPE_BENEFITS THEN
            V_DUES_SERIAL := SALARY_DUES_TB_SEQ.NEXTVAL;
            INSERT INTO GFC.SALARY_DUES_TB (
                SERIAL, EMP_NO, THE_MONTH, PAY_TYPE, PAY, THE_DATE, NOTE, ENTRY_USER, STATUS
            ) VALUES (
                V_DUES_SERIAL, D.EMP_NO, V_MONTH, V_PT, V_DA, SYSDATE,
                SUBSTR('REQ_ID=' || TO_CHAR(V_REQ_ID) || ' D=' || TO_CHAR(D.DETAIL_ID) || ' - ' || NVL(D.NOTE, 'Payment'), 1, 200),
                V_USER, 1
            );
          ELSE
            V_DUES_SERIAL := NULL;
          END IF;
          UPDATE GFC.PAYMENT_REQ_DETAIL_TB SET
              REQ_AMOUNT = V_AMT, PAY_TYPE = V_PT, DUES_AMOUNT = V_DA,
              DETAIL_STATUS = C_REQ_STATUS_PAID, POST_SERIAL_DUES = V_DUES_SERIAL
           WHERE DETAIL_ID = D.DETAIL_ID;
          V_OK := V_OK + 1;
        END IF;
      END IF;
    END LOOP;

    IF V_REQ_ID IS NULL THEN P_MSG_OUT := 'لم يتم العثور على سجلات'; RETURN; END IF;

    V_NEW_ST := CALC_MASTER_STATUS(V_REQ_ID);
    UPDATE GFC.PAYMENT_REQ_TB SET
        STATUS = V_NEW_ST,
        PAY_USER = CASE WHEN V_NEW_ST IN (C_REQ_STATUS_PAID, C_REQ_STATUS_PARTIAL_PAID) THEN V_USER ELSE PAY_USER END,
        PAY_DATE = CASE WHEN V_NEW_ST IN (C_REQ_STATUS_PAID, C_REQ_STATUS_PARTIAL_PAID) THEN SYSDATE ELSE PAY_DATE END,
        UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE
     WHERE REQ_ID = V_REQ_ID;

    SYNC_MASTER_AMOUNT(V_REQ_ID);
    LOG_ACTION(V_REQ_ID, 'PAY', V_OLD_ST, V_NEW_ST, 'OK=' || V_OK || ' ERR=' || V_ERR);

    IF V_ERR > 0 AND V_OK > 0 THEN
      P_MSG_OUT := '1|تم صرف ' || V_OK || ' موظف - فشل ' || V_ERR || ':' || CHR(10) || SUBSTR(V_FAIL_LIST, 1, 3000);
    ELSIF V_ERR > 0 THEN
      P_MSG_OUT := 'فشل صرف ' || V_ERR || ' موظف:' || CHR(10) || SUBSTR(V_FAIL_LIST, 1, 3500);
    ELSE
      P_MSG_OUT := '1';
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  PROCEDURE DETAIL_LIST (
      P_REQ_ID NUMBER, P_REF_CUR_OUT OUT SYS_REFCURSOR, P_MSG_OUT OUT VARCHAR2
  ) IS
  BEGIN
    OPEN P_REF_CUR_OUT FOR
      SELECT D.DETAIL_ID, D.REQ_ID, D.EMP_NO,
             E.NAME AS EMP_NAME,
             EMP_PKG.GET_EMP_BRANCH_NAME(D.EMP_NO) AS BRANCH_NAME,
             D.REQ_AMOUNT, D.DUES_AMOUNT,
             D.PAY_TYPE, D.LIMIT_FLAG, D.DETAIL_STATUS,
             SETTING_PKG.CONSTANT_DETAILS_TB_GET_NAME(541, D.DETAIL_STATUS) AS DETAIL_STATUS_NAME,
             CASE WHEN M.REQ_TYPE = C_REQ_TYPE_BENEFITS THEN GET_CONSTANT_NAME(D.PAY_TYPE) ELSE GET_DUES_TYPE_NAME(D.PAY_TYPE) END AS PAY_TYPE_NAME,
             D.POST_SERIAL_DUES, D.NOTE
        FROM GFC.PAYMENT_REQ_DETAIL_TB D
        JOIN DATA.EMPLOYEES E ON E.NO = D.EMP_NO
        JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
       WHERE D.REQ_ID = P_REQ_ID
       ORDER BY D.EMP_NO;
    P_MSG_OUT := '1';
  EXCEPTION WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;


  -- ============================================================
  -- Batch Lookup
  -- ============================================================
  FUNCTION GET_DETAIL_BATCH_ID (P_DETAIL_ID NUMBER) RETURN NUMBER IS
    V_ID NUMBER;
  BEGIN
    SELECT L.BATCH_ID INTO V_ID
    FROM GFC.PAYMENT_BATCH_LINK_TB L
    JOIN GFC.PAYMENT_BATCH_TB B ON B.BATCH_ID = L.BATCH_ID
    WHERE L.DETAIL_ID = P_DETAIL_ID AND B.STATUS <> 9 AND ROWNUM = 1;
    RETURN V_ID;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  FUNCTION GET_DETAIL_BATCH_NO (P_DETAIL_ID NUMBER) RETURN VARCHAR2 IS
    V_NO VARCHAR2(20);
  BEGIN
    SELECT B.BATCH_NO INTO V_NO
    FROM GFC.PAYMENT_BATCH_TB B
    JOIN GFC.PAYMENT_BATCH_LINK_TB L ON L.BATCH_ID = B.BATCH_ID
    WHERE L.DETAIL_ID = P_DETAIL_ID AND B.STATUS <> 9 AND ROWNUM = 1;
    RETURN V_NO;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  FUNCTION GET_REQ_BATCH_ID (P_REQ_ID NUMBER) RETURN NUMBER IS
    V_ID NUMBER;
  BEGIN
    SELECT MAX(L.BATCH_ID) INTO V_ID
    FROM GFC.PAYMENT_BATCH_LINK_TB L
    JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.DETAIL_ID = L.DETAIL_ID
    JOIN GFC.PAYMENT_BATCH_TB B ON B.BATCH_ID = L.BATCH_ID
    WHERE D.REQ_ID = P_REQ_ID AND B.STATUS <> 9;
    RETURN V_ID;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;

  FUNCTION GET_REQ_BATCH_NO (P_REQ_ID NUMBER) RETURN VARCHAR2 IS
    V_ID NUMBER;
    V_NO VARCHAR2(20);
  BEGIN
    V_ID := GET_REQ_BATCH_ID(P_REQ_ID);
    IF V_ID IS NULL THEN RETURN NULL; END IF;
    SELECT BATCH_NO INTO V_NO FROM GFC.PAYMENT_BATCH_TB WHERE BATCH_ID = V_ID;
    RETURN V_NO;
  EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL; END;


