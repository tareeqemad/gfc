  -- ===========================================================
  -- CHECK_DUPLICATE_EMP
  -- Returns REQ_NO if employee exists in another active request
  -- of the same type/month, or '0' if clear
  -- ===========================================================
  PROCEDURE CHECK_DUPLICATE_EMP (
      P_EMP_NO NUMBER, P_REQ_ID NUMBER,
      P_MSG_OUT OUT VARCHAR2
  ) IS
    V_MONTH NUMBER; V_TYPE NUMBER; V_REQ_NO VARCHAR2(50);
  BEGIN
    SELECT THE_MONTH, REQ_TYPE INTO V_MONTH, V_TYPE
      FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID;

    BEGIN
      SELECT M.REQ_NO INTO V_REQ_NO
        FROM GFC.PAYMENT_REQ_DETAIL_TB D
        JOIN GFC.PAYMENT_REQ_TB M ON M.REQ_ID = D.REQ_ID
       WHERE D.EMP_NO = P_EMP_NO
         AND D.DETAIL_STATUS <> C_REQ_STATUS_CANCELLED
         AND M.STATUS <> C_REQ_STATUS_CANCELLED
         AND M.THE_MONTH = V_MONTH AND M.REQ_TYPE = V_TYPE
         AND M.REQ_ID <> P_REQ_ID
         AND ROWNUM = 1;
      P_MSG_OUT := V_REQ_NO;
    EXCEPTION WHEN NO_DATA_FOUND THEN P_MSG_OUT := '0';
    END;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := '0';
    WHEN OTHERS THEN P_MSG_OUT := '0';
  END;


  -- ===========================================================
  -- DETAIL_PREVIEW_SINGLE
  -- Preview calculated amount for a single employee before insert
  -- Returns: "CALC_AMOUNT|DUES_AVAILABLE|LIMIT_FLAG" or "ERR|message"
  -- ===========================================================
  PROCEDURE DETAIL_PREVIEW_SINGLE (
      P_REQ_ID NUMBER, P_EMP_NO NUMBER, P_REQ_AMOUNT NUMBER DEFAULT 0,
      P_MSG_OUT OUT VARCHAR2
  ) IS
    V_ST NUMBER; V_TYPE NUMBER; V_CALC NUMBER; V_PCT NUMBER; V_MONTH NUMBER;
    V_MASTER_PT NUMBER; V_LV NUMBER; V_HV NUMBER;
    V_MONTH2 NUMBER; V_AMT NUMBER; V_PT NUMBER; V_DA NUMBER;
    V_LF VARCHAR2(3); V_MSG VARCHAR2(4000);
    V_DUES_AVAIL NUMBER; V_323 NUMBER;
  BEGIN
    SELECT STATUS, REQ_TYPE, CALC_METHOD, PERCENT_VAL, THE_MONTH, PAY_TYPE, L_VALUE, H_VALUE
      INTO V_ST, V_TYPE, V_CALC, V_PCT, V_MONTH, V_MASTER_PT, V_LV, V_HV
      FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID;

    -- نوع 5: بدون حساب مبلغ — بس الاسم
    IF V_TYPE = C_REQ_TYPE_BENEFITS THEN
      P_MSG_OUT := '0|0||0|' || NVL(EMP_PKG.GET_EMP_NAME(P_EMP_NO), '');
      RETURN;
    END IF;

    PREPARE_REQUEST_VALUES(P_EMP_NO, V_MONTH, V_TYPE,
        V_CALC, V_PCT, P_REQ_AMOUNT, V_MASTER_PT,
        NULL, P_REQ_ID, V_LV, V_HV,
        V_MONTH2, V_AMT, V_PT, V_DA, V_LF, V_MSG);

    IF V_MSG IS NOT NULL THEN
      P_MSG_OUT := 'ERR|' || V_MSG;
    ELSE
      -- رصيد المستحقات الطبيعي (بدون طرح المحجوز — المحجوز فقط بعد الاعتماد)
      V_DUES_AVAIL := GREATEST(GET_EMP_DUES_BASE(P_EMP_NO)
                        + GET_EMP_DUES_ADD(P_EMP_NO)
                        - GET_EMP_DUES_DED(P_EMP_NO), 0);
      BEGIN
        SELECT NVL(SUM(S.VALUE),0) INTO V_323
          FROM DATA.SALARY S WHERE S.EMP_NO = P_EMP_NO AND S.CON_NO = C_SALARY_CON_DUES AND S.MONTH = V_MONTH;
      EXCEPTION WHEN OTHERS THEN V_323 := 0;
      END;
      -- FORMAT: CALC_AMOUNT|DUES_AVAILABLE|LIMIT_FLAG|SAL_323|EMP_NAME
      P_MSG_OUT := TO_CHAR(NVL(V_AMT,0)) || '|' || TO_CHAR(NVL(V_DUES_AVAIL,0)) || '|' || NVL(V_LF, '') || '|' || TO_CHAR(NVL(V_323,0)) || '|' || NVL(EMP_PKG.GET_EMP_NAME(P_EMP_NO), '');
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'ERR|الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := 'ERR|' || SQLERRM;
  END;


  -- ===========================================================
  -- PAYMENT_REQ_UNAPPROVE — إلغاء الاعتماد (رجوع لمسودة)
  -- ===========================================================
  PROCEDURE PAYMENT_REQ_UNAPPROVE (P_REQ_ID NUMBER, P_NOTE VARCHAR2 DEFAULT NULL, P_MSG_OUT OUT VARCHAR2) IS
    V_ST NUMBER; V_USER NUMBER; V_BATCH_CNT NUMBER;
  BEGIN
    SELECT STATUS INTO V_ST FROM GFC.PAYMENT_REQ_TB WHERE REQ_ID = P_REQ_ID FOR UPDATE NOWAIT;

    -- فحص: مش مصروف
    IF V_ST = C_REQ_STATUS_PAID THEN P_MSG_OUT := 'لا يمكن إلغاء اعتماد طلب مصروف'; RETURN; END IF;

    -- فحص: مش محتسب بدفعة
    SELECT COUNT(1) INTO V_BATCH_CNT
      FROM GFC.PAYMENT_BATCH_LINK_TB L
      JOIN GFC.PAYMENT_BATCH_TB B ON B.BATCH_ID = L.BATCH_ID
      JOIN GFC.PAYMENT_REQ_DETAIL_TB D ON D.DETAIL_ID = L.DETAIL_ID
     WHERE D.REQ_ID = P_REQ_ID AND B.STATUS <> 9;
    IF V_BATCH_CNT > 0 THEN P_MSG_OUT := 'الطلب محتسب ضمن دفعة — يجب فك الاحتساب أولاً'; RETURN; END IF;

    V_USER := USER_PKG.GET_USER_ID;

    -- إرجاع كل الديتيل المعتمدة لمسودة
    UPDATE GFC.PAYMENT_REQ_DETAIL_TB
       SET DETAIL_STATUS = C_REQ_STATUS_DRAFT
     WHERE REQ_ID = P_REQ_ID AND DETAIL_STATUS = C_REQ_STATUS_APPROVED;

    -- تحديث حالة الماستر
    UPDATE GFC.PAYMENT_REQ_TB SET
        STATUS = C_REQ_STATUS_DRAFT,
        APPROVE_USER = NULL, APPROVE_DATE = NULL,
        UPDATE_USER = V_USER, UPDATE_DATE = SYSDATE,
        NOTE = CASE WHEN P_NOTE IS NOT NULL THEN P_NOTE ELSE NOTE END
     WHERE REQ_ID = P_REQ_ID;

    LOG_ACTION(P_REQ_ID, 'UNAPPROVE', V_ST, C_REQ_STATUS_DRAFT, NVL(P_NOTE, 'إلغاء اعتماد'));
    P_MSG_OUT := '1';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN P_MSG_OUT := 'الطلب غير موجود';
    WHEN OTHERS THEN P_MSG_OUT := SQLERRM;
  END;

