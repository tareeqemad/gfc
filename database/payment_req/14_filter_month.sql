-- ============================================================
-- FILTER_MONTH — عمود اختياري لفلترة الموظفين بشهر محدد
-- (للنوع 3 — دفعة من المستحقات — بدون ربط مالي)
-- ============================================================
-- الفكرة:
--   النوع 3 يصرف من رصيد المستحقات العام (لا علاقة بالشهر مالياً)
--   لكن المحاسب أحياناً بدّو يصرف فقط للموظفين المحتسبين في شهر معيّن
--   (مثلاً: "اصرف لكل اللي في كشف 09/2025 بس على رصيدهم العام")
--   FILTER_MONTH = هذا الشهر — يُستخدم في BULK_PREVIEW كفلتر إضافي
--   THE_MONTH يبقى للحفاظ على الـ NOT NULL constraint (يُحفظ كـ شهر افتراضي)
-- ============================================================

ALTER TABLE GFC.PAYMENT_REQ_TB ADD (
    FILTER_MONTH    NUMBER(6)  NULL    -- YYYYMM — اختياري، فلتر الجلب فقط
);

COMMENT ON COLUMN GFC.PAYMENT_REQ_TB.FILTER_MONTH IS
  'فلتر شهر الاحتساب (اختياري — يُستخدم لجلب الموظفين فقط، بدون أي ربط مالي). متوفر للنوع 3.';

