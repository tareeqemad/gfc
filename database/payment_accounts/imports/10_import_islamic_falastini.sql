        DBMS_OUTPUT.PUT_LINE('  No BRANCH for PRINT_NO=' || R.BNO || ' (EMP=' || V_EMP_NO || ')');
      END;
    END IF;

    -- 3) Check if account exists for this employee + provider
    SELECT COUNT(*) INTO V_EXISTS
      FROM GFC.PAYMENT_ACCOUNTS_TB
     WHERE EMP_NO = V_EMP_NO
       AND PROVIDER_ID = V_PROVIDER_ID
       AND STATUS = 1;

    IF V_EXISTS > 0 THEN
      -- Update existing
      UPDATE GFC.PAYMENT_ACCOUNTS_TB
         SET ACCOUNT_NO  = R.ACNT_NO,
             BRANCH_ID   = V_BRANCH_ID,
             IBAN        = NVL(R.IBAN, IBAN),
             NOTES       = TRIM(NVL(NOTES,'') || ' [Imported bank: ' || TO_CHAR(SYSDATE,'YYYY-MM-DD') || ']'),
             UPDATE_DATE = SYSDATE
       WHERE EMP_NO = V_EMP_NO
         AND PROVIDER_ID = V_PROVIDER_ID
         AND STATUS = 1;
      V_UPDATED := V_UPDATED + 1;
    ELSE
      -- Insert new
      INSERT INTO GFC.PAYMENT_ACCOUNTS_TB (
        ACC_ID, EMP_NO, PROVIDER_ID, BRANCH_ID,
        ACCOUNT_NO, IBAN,
        IS_DEFAULT, SPLIT_TYPE, IS_ACTIVE, STATUS,
        ENTRY_DATE, NOTES
      ) VALUES (
        GFC_PAK.PAYMENT_ACCOUNTS_SEQ.NEXTVAL, V_EMP_NO, V_PROVIDER_ID, V_BRANCH_ID,
        R.ACNT_NO, R.IBAN,
        1, 3, 1, 1,
        SYSDATE, 'Imported from bank template'
      );
      V_INSERTED := V_INSERTED + 1;
    END IF;
  END LOOP;

  COMMIT;
  DBMS_OUTPUT.PUT_LINE('========================================');
  DBMS_OUTPUT.PUT_LINE('Inserted:    ' || V_INSERTED);
  DBMS_OUTPUT.PUT_LINE('Updated:     ' || V_UPDATED);
  DBMS_OUTPUT.PUT_LINE('No EMP:      ' || V_NO_EMP);
  DBMS_OUTPUT.PUT_LINE('No BRANCH:   ' || V_NO_BRANCH);
  DBMS_OUTPUT.PUT_LINE('========================================');
END;
